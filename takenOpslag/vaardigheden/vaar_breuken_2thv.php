	<h3>Opdracht 9 De erfenis</h3>
	<p>
		Een oude indiaan in het Andesgebergte schrijft in zijn testament hoe na zijn dood zijn kudde lama’s verdeeld wordt over zijn vier zonen. Volgens dit testament zal de oudste zoon een derde deel van de kudde krijgen, de tweede zoon een kwart en de jongste twee zonen (een tweeling) elk een vijfde van de kudde.
	</p>
	<img src="../afbeeldingen/vaar_breuken_2thv/lama.png" alt="lama" width="200" class="rightfloat" height="" />

	<p>
		Een half jaar later sterft de indiaan en in de kudde zijn op dat moment 59 lama’s. De vier zonen zitten met hun handen in het haar, want hoe moeten zij die nu verdelen? Ze vragen raad aan een indiaan die als zeer wijs bekend staat.	Die zegt: ik heb 1 lama, die kun je van mij lenen en gebruiken bij de verdeling.
	</p>
	
	<p>
		Nu zijn er 60 lama’s en de oudste zoon krijgt dus 20 lama’s, de tweede zoon 15 en de jongste twee zonen elk 12 lama’s. Samen zijn dat er 59 en zo krijgt de wijze indiaan zijn lama terug.
	</p>
	<p>
Een raar verhaal, maar met breuken kun je uitleggen hoe dat kan. Probeer het maar.
	</p>
	
	<div class="hider">
		<div class="showhide">&rarr;</div>
		<div class="antwoord">
			<h3>Antwoorden bij opdracht 9</h3>
			<p>
				Er zijn meerdere manieren om dit uit te leggen.
			</p>
			<p class="breuken">
				De oudste zoon krijgt 20 van de 60 lama's, dat is `20/60` deel.<br>
				De tweede zoon krijgt 15 van de 60 lama's, dat is `15/60` deel.<br>
				De jongsten krijgen samen 24 van de 60 lama's, dat is `24/60` deel.<br>
				De wijze man krijgt 1 van de 60 lama's, dat is `1/60` deel.<br>
				`20/60 + 15/60 + 24/60 + 1/60 = 60/60`<br>
			</p>
			<p class="breuken">
				Andere manier:<br>
				`1/3 + 1/4 + 1/5 + 1/5 + 1/60 = 20/60 + 15/60 + 12/60 + 12/60 + 1/60 = 60/60`
