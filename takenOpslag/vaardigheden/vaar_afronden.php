<div class="tekstblok theorie vaardigheden">
<h3>Afronden</h3>
	<p>
		Kommagetallen worden vaak afgerond. Een getal met veel cijfers achter de komma is moeilijk leesbaar. Bovendien zijn vaak alleen de eerste paar cijfers belangrijk.
	</p>
	<p>
		Bij afronden is altijd de vraag of je naar boven of naar onder afrond. Je kijkt altijd naar het cijfer ná het cijfer waarop je wilt afronden.	Je rondt het getal af naar boven als het eerste weggelaten cijfer <strong>5 of hoger</strong> is. Bekijk de voorbeelden.
	</p>

	<p>
		<u>Voorbeeld 1</u>:
	</p>
	<p>
		We gaan het getal <strong>5,8125</strong> afronden op twee cijfers achter de komma, oftewel op twee decimalen.
	</p>
	<table class="breuken">
				<tbody>
				<tr>
					<td>
						Stap 1
					</td>
					<td>
						5,81<strong>2</strong>5
					</td>
					<td>
						kijk naar het <b>derde</b> cijfer achter de komma
					</td>
				</tr>
				<tr>
					<td>
						Stap 2
					</td>
					<td>
						2 &#60; 5				
					</td>
					<td>
						2 is kleiner dan 5
					</td>
				</tr>
				<tr>
					<td>
						Stap 3
					</td>
					<td>
						5,81
					</td>
					<td>
						laat alle cijfers na het tweede weg
					</td>
				</tr>
			</tbody>
			</table>
	<p>
		5,8125 afgerond op twee decimalen wordt <strong>5,81</strong>.
	</p>
	
	<p>
		<u>Voorbeeld 2</u>:
	</p>
	<p>
		Nu gaan we het getal <strong>6,738</strong> afronden op twee decimalen.
	</p>
	<table class="breuken">
				<tbody>
				<tr>
					<td>
						Stap 1
					</td>
					<td>
						6,73<strong>8</strong>
					</td>
					<td>
						kijk naar het <b>derde</b> cijfer achter de komma
					</td>
				</tr>
				<tr>
					<td>
						Stap 2
					</td>
					<td>
						8 &#62; 5				
					</td>
					<td>
						8 is groter dan 5
					</td>
				</tr>
				<tr>
					<td>
						Stap 3
					</td>
					<td>
						3 + 1 = 4
					</td>
					<td>
						tel 1 op bij het <b>tweede</b> getal achter de komma
					</td>
				</tr>
				<tr>
					<td>
						Stap 4
					</td>
					<td>
						6,7<b>4</b>
					</td>
					<td>
						laat de cijfers na de 4 weg
					</td>
				</tr>
			</tbody>
			</table>
	<p>
		6,738 afgerond op twee decimalen wordt <strong>6,74</strong>.
	</p>
<br>
	<h3>Afronden op hoeveel decimalen</h3>
	<p>
		Een "krom" getal met veel decimalen (cijfers achter de komma) kun je&nbsp;op verschillende manieren&nbsp;afronden:
		</p>
		<ul>
			<li>5,27439 is afgerond op 1 cijfer achter de komma: 5,3 <span style="color: #888888;">(naar boven afgerond)</span></li>
			<li>5,27439 is afgerond op 2 cijfers achter de komma: 5,27</li>
			<li>5,27439 is afgerond op 3 cijfers achter de komma: 5,274</li>
			<li>5,27439 is afgerond op&nbsp;4 cijfers achter de komma: 5,2744 <span style="color: #888888;">(naar boven afgerond)</span></li>
		</ul>
		<br>
	
	<h3>Let alleen op het eerstvolgende cijfer.</h3>
	<p>Als je een getal moet afronden, let dan alleen op het eerstvolgende cijfer dat je weglaat. Als dat cijfer lager is dan 5, rond je omlaag af. Is dat cijfer 5 of hoger, rond je omhoog af.</p>
	<p>Alle volgende cijfers zijn niet van belang.</p>
	<p>Afronden op een geheel getal (nul decimalen achter de komma):</p>
	<ul>
		<li>11,<strong>4</strong>325698476 wordt 11</li>
		<li>11,<strong>4</strong>982757812 wordt 11</li>
		<li>11,<strong>5</strong>023485645 wordt 12</li>
		<li>11,<strong>9</strong>998171544 wordt 12</li>
	</ul>
	<p>Afronden op een cent (één decimaal achter de komma):</p>
	<ul>
		<li>€ 11,43<strong>2</strong>5698476 wordt € 11,43</li>
		<li>€ 11,49<strong>8</strong>2757812 wordt € 11,50</li>
		<li>€ 11,50<strong>2</strong>3485645 wordt € 11,50</li>
		<li>€ 11,78<strong>5</strong>2340985 wordt € 11,79</li>
		<li>€ 11,99<strong>9</strong>8171544 wordt € 12,00</li>
	</ul>
