<div class="tekstblok theorie meetkunde">
	<h3>Theorie A Balk, kubus en prisma</h3>
	<p>
		Een <b>balk</b> is een drie dimensionaal figuur waarvan <u>alle vlakken rechthoeken</u> zijn (of één paar vierkanten en vier rechthoeken).
	</p>
	<p>
		Van een <b>kubus</b> zijn <u>alle vlakken even grote vierkanten</u>. Als je van één ribbe de lengte weet, weet je alles over deze ruimtefiguur.
	</p>
	<p>
		<img src="afbeeldingen/meet_ruimtefiguren/broodsnijmachine.png" width="150px" class="rightfloat">
		Een <b>prisma</b> kun je vergelijken met een brood. Als je een brood door een broodsnijmachine haalt, ontstaan er allemaal boterhammen die precies hetzelfde zijn, tenminste als je hem in de juiste richting in de machine stopt.<br>
		De oppervlakte van één zo'n boterham wordt het <b>grondvlak</b> genoemd (zie het bruine vlak in de afbeelding hiernaast).
	</p>
	<p>
		<img src="afbeeldingen/meet_ruimtefiguren/brood_th.png" width="200px" class="rightfloat">
		De bouwplaat van een prisma bestaat altijd uit rechthoeken. Alleen de twee vlakken van het grondvlak kunnen een andere vorm hebben.
	</p>
	<p>
		<u>Let op</u>: Het brood van hiernaast is ook een prisma. De bouwplaat van het brood bestaat uit twee boterhamvormen en vier rechthoeken. De bovenkant van het brood wordt een <b>gebogen vlak</b> genoemd.
	</p>
</div>

