<div class="tekstblok theorie meetkunde">
	<h3>Theorie B Inhoud van prisma's berekenen</h3>
	<p>
		De inhoud van een kubus of balk kun je berekenen met de formule<br>
		<nobr><em>Inhoud balk</em> = <em>lengte</em> &times; <em>breedte</em> &times; <em>hoogte</em></nobr>.
	</p>
	<p>
		Je weet dat <nobr><em>lengte</em> &times; <em>breedte</em> = <em>oppervlakte van het grondvlak</em></nobr> (afgekort <nobr><em>opp grondvlak</em></nobr>). Dus de inhoud van een kubus of balk kun je ook berekenen met de formule<br>
		<nobr><em>Inhoud balk</em> = <em>opp grondvlak</em> &times; <em>hoogte</em></nobr>.
	</p>
	<p>
		Sterker nog, de inhoud van een piramide bereken je met de formule:<br>
		<nobr><em>Inhoud <b>prisma</b></em> = <em>opp grondvlak</em> &times; <em>hoogte</em></nobr>.
	</p>
	<p>
