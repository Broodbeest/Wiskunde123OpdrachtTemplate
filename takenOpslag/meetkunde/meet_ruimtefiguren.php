<div class="tekstblok theorie meetkunde">
	<h3>Theorie E Parallelprojectie of perspectief</h3>
	<p>
		<img src="afbeeldingen/meet_ruimtefiguren/parallel_kubus_th.png" width="150px" class="rightfloat">
		Tot nog toe zijn in deze taak alle ruimtefiguren in <b>parallelprojectie</b> getekend. Dat kun je zien doordat <u>lijnen die in het echt evenwijdig zijn dat in de tekening ook zijn</u>. Zie de afbeelding hiernaast.
	</p>
	<p>
		Parallelprojectie wordt veel gebruikt bij het maken van <u>technische tekeningen</u>:<br>
		een architect die tekent wat de aannemer moet maken wil natuurlijk dat een kamer aan de voorkant net zo breed is als aan de achterkant.
	</p>
	<p>
		<img src="afbeeldingen/meet_ruimtefiguren/perspectief_vb.png" width="250px" class="rightfloat">
		Wanneer je een foto bekijkt zie je dat lijnen die in het echt evenwijdig zijn op papier helemaal niet evenwijdig zijn. Een tekening is <b>in perspectief</b> getekend als lijnen die in het echt evenwijdig zijn op papier in één punt snijden; het <b>verdwijnpunt</b>. Het verdwijnpunt ligt meestal op de horizon.
	</p>
	<p>
		<img src="afbeeldingen/meet_ruimtefiguren/perspectief_kubus_vb.png" width="600px" class=""><br>
		Perspectief tekenen wordt gebruikt in de kunst, maar ook als een architect een tekening maakt om te laten zien hoe het huis eruit gaat zien.
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
		<img src="afbeeldingen/meet_ruimtefiguren/perspectief_huis_opdr.png" width="200px" class="">
		<img src="afbeeldingen/meet_ruimtefiguren/parallel_huis_opdr.png" width="150px" class=""><br>
		<ol type="a">
			Eén van deze tekeningen is gestart in parallelprojectie.
			<li>
				Maak de tekening af.
			</li>
			<li>
				Maak ook de perspectief tekening af.
			</li>
		</ol>
	</p>
	<p>
		<u>Uitwerking</u>:<br>
		<ol type="a">
			<li>
				<img src="afbeeldingen/meet_ruimtefiguren/parallel_huis_antw.png" width="150px" class="rightfloat">
				De tweede tekening moet wel de parallelprojectie zijn; als je lijnen op de grond bestudeert, zie je dat ze écht parallel lopen.
			</li>
			<li>
				Trek eerst lijnen door die op de grond liggen, zo vindt je de verdwijnpunten. Maak daarna de tekening af.<br>
				<img src="afbeeldingen/meet_ruimtefiguren/perspectief_huis_antw.png" width="400px" class="rightfloat">
