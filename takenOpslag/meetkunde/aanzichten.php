<div class="tekstblok theorie meetkunde">
	<h3>Theorie C Kubussen</h3>
	<p>
	Hier zie je drie aanzichten van een stapel kubussen.
	</p>
	<img src="afbeeldingen/aanzichten/aanzichten_kubussen.jpg" alt="aanzichten_kubussen" height="204" width="275" class="rightfloat"/>
	<p>
	Om volledig te zijn zie je in het <b>bovenaanzicht</b> hoeveel kubussen er op elkaar liggen; de cijfers geven het aantal kubussen aan. Het is vergelijkbaar met een soort <b>hoogtekaart</b>.
	</p>
	<p>Eigenlijk zijn dan het vooraanzicht en het zijaanzicht overbodig geworden. Maar als je niet in één van de aanzichten aangeeft hoeveel kubussen er boven elkaar of achter elkaar of naast elkaar liggen, heb je aan de aanzichten alleen niet genoeg om de kubusstapel te bouwen. Er zijn dan meerdere mogelijkheden.
	<p>
</div>

