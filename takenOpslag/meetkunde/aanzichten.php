<div class="tekstblok theorie meetkunde">
	<h3>Theorie B Drieaanzicht</h3>
	<p>
	<img src="afbeeldingen/aanzichten/vogelhuisje.jpeg" alt="vogelhuisje" width="" height="" class="rightfloat"/>
	Bekijk de afbeelding van het vogelhuisje. Stel je voor dat de hele figuur in een doorzichtige balk past. Op de voorkant van de balk teken je wat je van die kant ziet: het vooraanzicht. Op de bovenkant komt het bovenaanzicht. Op de zijkant komt het zijaanzicht.
	</p>
	<p>
Als je deze drie aanzichten zo in één figuur zet, dat je die balk weer kunt vouwen, spreek je van een <b>drieaanzicht</b> van de figuur.
	</p>
