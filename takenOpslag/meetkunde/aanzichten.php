	<h3>Theorie D <em>Aanpak</em></h3>
	<p>
	Als je een bovenaanzicht hebt, kun je bedenken hoe het voor- en het zijaanzicht eruit ziet.
	</p>
	<img src="afbeeldingen/aanzichten/stappenplan1.png" alt="stappenplan" width="" height="" class="rightfloat" />
	<p><u>Voorbeeld</u></p>
	<p>
	Maak een vooraanzicht en een zijaanzicht bij het bovenaanzicht hiernaast.
	</p>
	<p>
	Bepaal per rij en kolom de hoogste waarde en zet die bijvoorbeeld naast en onder het bovenaanzicht. Gebruik die waarden om het vooraanzicht en zijaanzicht te tekenen.
	</p>

<img src="afbeeldingen/aanzichten/stappenplan2.png" alt="stappenplan" width="" height=""  />
	<p>
	Andersom kun je ook <b>een</b> bovenaanzicht bepalen als je enkel het voor- en zijaanzicht kent.
	</p>
