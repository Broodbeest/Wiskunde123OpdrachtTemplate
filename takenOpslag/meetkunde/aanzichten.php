<div class="tekstblok opdracht">
	<h3>Opdracht 1 Eenheden</h3>
		<ol>
			<li>
				Welke eenheid hoort er bij de afmetingen van de aanzichten van de auto uit het voorbeeld? Zoom desnoods in om een goed idee te krijgen.
			</li>
			<li>
				Waarom is het handiger om die afmetingen in een aanzicht te zetten en niet op een foto?
			</li>
		</ol>
		
		<div class="hider">
			<div class="showhide">&rarr;</div>
			<div class="antwoord">
				<ol>
					<li>
						De bijbehorende eenheid zal millimeter zijn. Het staat nergens in de aanzichten. Het is aannemelijk dat de auto niet 1460 meter hoog is of 1460 cm (= 14,60 meter). Maar 1460 mm = 1,460 m en uit ervaring weet ik dat ik gewoon over dit type audi heen kan kijken. Waarmee ik niet wil zeggen dat ik zo'n audi heb.
					</li>
					<li>
						In een foto heb je perspectief, waardoor je (de meeste) afmetingen niet waarheidsgetrouw kunt meten.
					</li>
				</ol>
			</div>
