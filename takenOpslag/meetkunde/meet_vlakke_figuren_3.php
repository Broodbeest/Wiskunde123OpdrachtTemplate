<!-- theorie F -->
<div class="tekstblok theorie meetkunde">
	<h3>Theorie F Vergrotingsfactor</h3>
	<p>
		Als twee figuren gelijkvormig zijn, kun je uitrekenen hoeveel groter het beeld is dan het origineel. Het getal 'hoeveel keer groter' noem je de <b>vergrotingsfactor</b>, afgekort <em>f</em>. Om de vergrotingsfactor te bepalen, zoek je eerst twee lengtes die bij elkaar horen. Daarna deel je 'lengte beeld' door 'lengte origineel':<br>
		<em>vergrotingsfactor</em> = <em>lengte beeld</em> : <em>lengte origineel</em><br>
		of<br>
		<em>f</em> = <em>beeld</em> : <em>origineel</em>
	</p>
	<p><img src="afbeeldingen/meet_vlakke_figuren_2/gelijkvormig_th2.png" width="300px" class="rightfloat">
		<u>Voorbeeld</u><br>
		Bekijk de driehoeken hiernaast. Het zijn dezelfde als in het vorige theorieblok, dus we weten dat &Delta;<em>ABC</em> ~ &Delta;<em>RPQ</em>.<br>
		Bereken de lengte van <em>RQ</em>.
	</p>
	<p>
		<u>Uitwerking</u><br>
		Let op de onderstreping in de gelijkvormigheidsnotatie: &Delta;<em><u>AB</u>C</em> ~ &Delta;<em><u>RP</u>Q</em>. Dus <em>AB</em> hoort bij bij <em>RP</em>. Gebruik dat om een verhoudingstabel in te vullen.
		<table class="verhouding">
			<tr>
				<td>&Delta;<em>ABC</em></td><td><em>AB</em> = 5</td><td><em>AC</em> = 4</td><td><em>BC</em> = 3</td><td rowspan="2"><img src="afbeeldingen/vaardigheden_verhoudingen/bopijlrechts.png" width="15px">&times; ...</td>
			</tr>
			<tr>
				<td>&Delta;<em>RPQ</em></td><td><em>PR</em> = 4</td><td><em>RQ</em> = ...</td><td><em>PQ</em> = 3,2</td>
			</tr>
		</table>
		<em>g</em> = <em>PR</em> : <em>AB</em> = 4 : 5 = 0,8<br>
		Je kunt ook doen: <em>g</em> = <nobr><em>PQ</em> : <em>BC</em></nobr> = <nobr>3,2 : 4</nobr>, maar daar komt ook 0,8 uit.<br>
		Nu kunnen we berekenen hoe groot <em>RQ</em> is: <nobr><em>BC</em> &sdot; 0,8</nobr> = <nobr>3 &sdot; 0,8 = 2,4</nobr>. Dus <nobr><em>PQ</em> = 2,4</nobr>.
	</p>
</div>

