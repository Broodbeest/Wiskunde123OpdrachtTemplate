<div class="tekstblok theorie meetkunde">
	<h3>Theorie G Vergroten en verkleinen</h3>
	<p>
		Als er sprake is van een vergroting, is de vergrotingsfactor groter dan 1. Als het beeld kleiner is dan het origineel, dan is de vergrotingsfactor kleiner dan 1; er is dus niet zo iets als een verkleiningsfactor.
	</p>
	<p>
		Twee theorieblokken terug leerde je dat twee driehoeken gelijkvormig zijn als alle hoeken in het origineel even groot zijn in het beeld.<br>
		In de afgelopen opdrachten heb je gezien dat voor alle lengtes die bij elkaar horen de vergrotingsfactor hetzelfde is. Dat kun je dus óók gebruiken om te checken of twee figuren gelijkvormig zijn.
	</p>
