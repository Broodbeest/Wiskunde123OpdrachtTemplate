	<div class="tekstblok theorie meetkunde">
		<h3>Inleiding Grote moskee van Cordoba, Cordoba, Spanje</h3>
		<img src="afbeeldingen/meetkunde_arabesk/cordoba.jpg" alt="cordoba" width="250" height="" class="rightfloat"/>
		<p>De Grote moskee van Cordoba, ook wel Mezquita genoemd, is een uniek architectonisch monument op de plaats waar ooit de Visigotische kerk Vincentius van Zaragoza stond. Die kerk was op de fundamenten van een Romeinse tempel gebouwd.</p>
		<p>Na de verovering van Córdoba in 711 door de Moren (de naam voor de islamitische bevolking van het middeleeuwse Spanje) werd vanaf de achtste eeuw een moskee gebouwd die sinds de christelijke herovering van Córdoba in 1236 als de kathedraal van het bisdom Córdoba in gebruik is, gewijd aan de Maria-Tenhemelopneming.</p>
		<p>Koning Ferdinand III van Castilië en keizer Karel V lieten de kerk uitbreiden en verfraaien. In de loop der eeuwen hebben verschillende verbouwingen plaatsgevonden, zodat tegenwoordig zowel de Moorse als de christelijke invloeden duidelijk herkenbaar zijn. Zowel het grondplan van een traditionele moskee als het Latijnse kruis voor een kathedraal zijn terug te vinden in het gebouw.</p>
	</div>

