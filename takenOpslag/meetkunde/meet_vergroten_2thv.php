<div class="tekstblok theorie meetkunde">
	<h3>Theorie A Groeifactor bij gelijkvormigheid</h3>
	<p>
		<img src="afbeeldingen/meet_vergroten_2thv/gelijkvormig_opdr1.png" width="300px" class="rightfloat">
		Twee vlakke figuren heten <b>gelijkvormig</b> als ze dezelfde vorm hebben, dus hoeken hetzelfde aantal graden groot zijn. Als twee vlakke figuren gelijkvormig zijn, hebben alle lengtes die bij elkaar horen steeds dezelfde <a href="index2.php?p=vaar_verhoudingen" target="_blank">verhouding</a> tot elkaar.
	</p>
	<p>
		In de opdracht hiervoor zag je dat &Delta;<em>ABC</em> gelijkvormig is met &Delta;<em>PQR</em>. In een verhoudingstabel (maar dan op zijn zij) ziet dat er zo uit:<br>
		<table class="verhouding">
			<tr>
				<td rowspan="2">&times;2 <img src="afbeeldingen/bopijllinks.png" height="50px"></td>
				<td><b>&Delta;<em>ABC</em></b></td>
				<td><em>AB</em> = 2</td>
				<td><em>AC</em> = ...</td>
				<td><em>BC</em> = 3</td>
				<td rowspan="2"><img src="afbeeldingen/bopijlrechts.png" height="50px"> &times;2</td>
			</tr>
			<tr>
				<td><b>&Delta;<em>PQR</em></b></td>
				<td><em>PQ</em> = ...</td>
				<td><em>PR</em> = 7,21</td>
				<td><em>QR</em> = 6</td>
			</tr>
		</table>
	</p>
	<p>
		De factor &times;2 heet ook wel de <b>groeifactor</b>, en doet precies hetzelfde als de groeifactor in <a href="index2.php?p=vaar_procentuele_groei" target="_blank">procentuele groei</a>.
	</p>
	<p>
		<img src="afbeeldingen/meet_vergroten_2thv/gelijkvormig_th.png" width="300px" class="rightfloat">
		<u>Voorbeeld</u>:<br>
		Gegeven is dat de vierhoeken <em>ABCD</em> en <em>EFGH</em> van hiernaast gelijkvormig zijn. Bepaal de groeifactor.
	</p>
	<p>
		<u>Uitwerking</u>:<br>
		Alleen zijde <em>EF</em> is bekend, dus daarmee moet de groeifactor worden bepaald. Zijde <em>CD</em> is de bijbehorende zijde van <em>EF</em>, kijk maar naar de hoeken.
		<table class="verhouding">
			<tr>
				<td colspan="2"><em>CD</em> = 4</td>
				<td colspan="2"><em>EF</em> = 3</td>
			</tr>
			<tfoot>
				<th></th>
				<th colspan="2"><img src="afbeeldingen/lrpijlonder.png" width="50px"><br>&times; groeifactor</th>
				<th></th>
			</tfoot>
		</table>
	</p>
	<p>
		En de groeifactor is `text{NIEUW}/text{OUD} = 3/4=0,75`.
	</p>
</div>

