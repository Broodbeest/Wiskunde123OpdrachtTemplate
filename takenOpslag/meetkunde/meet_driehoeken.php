<!-- theorie B -->
<div class="tekstblok theorie meetkunde">
	<h3>Theorie B De drie bijzondere driehoeken</h3>
	<ol>
		<li>
			De <b>rechthoekige driehoek</b> heeft
			<ul>
				<li>
					altijd één rechte hoek (dus een hoek van 90&deg;).
				</li>
			</ul>
		</li>
		<img src="afbeeldingen/meet_driehoeken/gelijkbenig_th.png" width="80px" class="rightfloat">
		<li>
			De <b>gelijkbenige driehoek</b> heeft
			<ul>
				<li>twee zijden (benen) die even lang zijn</li>
				<li>twee <b>basishoeken</b> die even groot zijn en</li>
				<li>de derde hoek heet de <b>tophoek</b>.</li>
			</ul>
			<u>Ezelsbruggetje</u>: net als een mens heeft een gelijk<u>benige</u> driehoek twee benen die even lang zijn. De twee hoeken die de benen met de grond maken zijn even groot (zie het sterretje).
		</li>
		<img src="afbeeldingen/meet_driehoeken/gelijkzijdigedriehoeken_th.png" width="200px" class="rightfloat">
		<li>
			De <b>gelijkzijdige driehoek</b> heeft
			<ul>
				<li>drie zijden die even lang zijn en</li>
				<li>drie hoeken die allemaal 60&deg; zijn.</li>
			</ul>
			In de taak <a href="index2.php?p=meet_bloem_thv">Bloem</a> heb je stiekem al een heleboel gelijkzijdige driehoeken getekend.
		</li>
	</ol>
</div>

