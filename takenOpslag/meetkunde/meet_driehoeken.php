<div class="tekstblok theorie meetkunde">
	<h3>Theorie C Hoekensom</h3>
	<p>
		<img src="afbeeldingen/meet_driehoeken/hoekensom_droog_th.png" width="160px" class="rightfloat">
		In elke driehoek (ook als de driehoek niet "bijzonder" is) geldt dat de som van de hoeken altijd 180&deg; is.
	</p>
	<p>
		Dus:<br>
		&ang;<em>A</em> + &ang;<em>B</em> + &ang;<em>C</em> = 180&deg;.
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
		Hiernaast zie je &Delta;<em>ABC</em>. &ang;<em>A</em> = 100&deg; en &ang;<em>B</em> = 50&deg;.<br>
		<img src="afbeeldingen/meet_driehoeken/hoekensom_th.png" width="200px" class="rightfloat">
		Bereken hoe groot &ang;<em>C</em> is.
	</p>
	<p>
		<u>Uitwerking</u>:<br>
		&ang;<em>A</em> + &ang;<em>B</em> + &ang;<em>C</em> = 180&deg;<br>
		100&deg; + 50&deg; + &ang;<em>C</em> = 180&deg;<br>
		150&deg; + &ang;<em>C</em> = 180&deg;<br>
		&ang;<em>C</em> = 30&deg;.
	</p>
