<div class="tekstblok theorie meetkunde">
	<h3>Theorie A Driehoek-notatie</h3>
	<p>
		<img src="afbeeldingen/meet_driehoeken/rechthoekig_opdr.png" width="150px" class="rightfloat">
		Hiernaast zie je een <b>driehoek</b> met de hoekpunten <em>A</em>, <em>B</em> en <em>C</em>. Dat kun je korter schrijven als &Delta;<em>ABC</em> en spreek je uit als "driehoek a b c".
	</p>
	<p>
		Als een driehoek een rechte hoek heeft, zoals in het plaatje hierboven, dan noem je de driehoek een <b>rechthoekige driehoek</b>. De zijden <em>AC</em> en <em>BC</em> zitten aan de rechte hoek (&ang;<em>C</em>) vast. Deze zijden noem je daarom de <b>rechthoekszijden</b>. De derde zijde (<em>AB</em>) is altijd het langst en noem je daarom de <b>langste</b> of <b>schuine zijde</b>.
	</p>
</div>

