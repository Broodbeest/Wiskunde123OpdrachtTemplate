<h1>Driehoeken</h1>

<!-- Inleiding -->
<div class="tekstblok theorie meetkunde">
	<h3>Inleiding Driehoeken</h3>
	<p>
		<img src="afbeeldingen/meet_driehoeken/gezicht_driehoeken.png" width="150px" class="rightfloat">
		In de geometrie (meetkunde) zijn driehoeken heel belangrijk. Je kunt alle figuren opdelen in allemaal driehoeken, ook <b>ruimtefiguren</b> (zie de figuur hiernaast).
	</p>
	<p>
		<img src="afbeeldingen/meet_driehoeken/driehoek.png" width="100px" class="leftfloat">
		<b>Driehoek</b><br>
		Een driehoek is een meetkundig figuur bestaande uit drie lijnen of lijnstukken die elkaar snijden in drie verschillende punten. Vandaar de naam: <u>Drie</u>hoek.
	</p>
	<p>
		In deze taak leer je de eigenschappen van de belangrijke driehoeken, de zogenaamde <b>bijzondere driehoeken</b>: de <b>gelijkbenige</b>, <b>gelijkzijdige</b> en <b>rechthoekige driehoek</b>.<br>
		Voor sommige opdrachten heb je het <a href="afbeeldingen/meet_driehoeken/werkblad_driehoeken.pdf" target="_blank">werkblad</a> nodig.
	</p>
