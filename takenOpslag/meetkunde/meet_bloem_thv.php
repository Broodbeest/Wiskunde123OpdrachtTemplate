<div class="tekstblok theorie meetkunde">
	<h3>Theorie A Middelpunt, straal en diameter</h3>
	<img src="../afbeeldingen/meetkunde_bloem/cirkel_straal_diameter_middelpunt.png" alt="cirkel, straal en diameter" width="300" height="" class="rightfloat"/>	
	<p>
		Precies in het midden ligt het <b>middelpunt</b> van de cirkel.
	</p>
	<p>
		De afstand van het middelpunt van een cirkel tot de cirkel zelf is steeds gelijk, deze afstand noemen we de <b>straal</b>.
	</p>
	<p>
		De afstand van de ene kant van de cirkel via het middelpunt naar de andere kant heet de <b>diameter</b>.
	</p>
	<p>
		De straal past tweemaal in de diameter.
	</p>
</div>

