<h1>Bloem</h1>

<div class="tekstblok theorie meetkunde">
	<h3>Inleiding</h3>
	
	<p><img src="afbeeldingen/meetkunde_bloem/cirkel.png" alt="Cirkel met middelpunt M" width="150" height="" class="rightfloat"/>
		Meetkunde is het deel van de wiskunde dat over <b>figuren</b> gaat, zowel platte als ruimtelijke figuren. Deze meetkunde taak gaat over <b>cirkels</b>. Een cirkel is heel bijzonder. Want de cirkel staat overal even ver van het <b>middelpunt</b>. 
	</p>	

	<h3>Doelen</h3>
	<ul>
		<li>met passer tekenen van cirkels met gegeven straal;</li>
		<li>trainen netheid en nauwkeurigheid;</li>
		<li>begrippen: cirkel, straal, diameter en middelpunt;</li>
		<li>verdelen van een cirkel in zes gelijke delen.</li>
	</ul>			
				
	<h3>Werkwijzer</h3>
	<p>
		Je maakt de opdrachten in je teken- en ruitjesschrift, op het <a href="afbeeldingen/meetkunde_bloem/werkblad_bloem_thv.pdf" target="_blank">werkblad</a> en op de DWO. Je sluit de taak af als:
		<ul>
			<li>werkblad en schriften zijn afgetekend;</li>
			<li>de opdrachten DWO zijn gemaakt;</li>
			<li>jouw 'Bloem', zie opdracht 9, is ingeleverd.</li>
		</ul>
	</p>
	<p>
		<b>Lees</b> de opdrachten steeds eerst goed door en <b>oefen</b> constructies eerst in je <b>tekenschrift</b> vóórdat je ze in het net maakt op je werkblad.
	</p>
</div>


