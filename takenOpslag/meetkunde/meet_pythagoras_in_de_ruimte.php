<div class="tekstblok theorie meetkunde">
	<h3>Theorie A Stelling van Pythagoras</h3>
	<p>
		<img src="afbeeldingen/meet_pythagoras_ruimte_hv/rechthoekig_th.png" width="250px" class="rightfloat">
		Om een zijde in een rechthoekige driehoek te berekenen gebruik je een tabelletje of de uitdrukking <em>a</em><sup>2</sup> + <em>b</em><sup>2</sup> = <em>c</em><sup>2</sup>.<br>
		Gebruik je het tabelletje, dan moet de schuine zijde onder de streep staan. Gebruik je de uitdrukking <em>a</em><sup>2</sup> + <em>b</em><sup>2</sup> = <em>c</em><sup>2</sup>, dan moet je de schuine zijde invullen op de plek van <em>c</em>.<br>
		<u>Let op</u>: als de driehoek niet rechthoekig is, werkt de stellig van Pythagoras niet.
	</p>
	<br>
	<p>
		<img src="afbeeldingen/meet_pythagoras_ruimte_hv/pythagoras_inl.png" width="200px" class="rightfloat">
		<u>Voorbeeld</u>:
		<br>
		Bereken de lengte van de onbekende zijde.
	</p>
	<p>
		<u>Uitwerking</u>:
		<br>
		Bedenk welke zijde de schuine zijde is. Dat is de langste zijde, of de zijde tegenover de rechte hoek. In dit voorbeeld is dat <em>BC</em>.
		<table>
			<tr>
				<td><em>Tabelletje</em></td><td><em>Uitdrukking</em></td>
			</tr>
			<tr>
				<td>
					Omdat <em>BC</em> de schuine zijde is komt die zijde onder de streep.
				</td>
				<td>
					Omdat <em>BC</em> de schuine zijde is, is dat <em>c</em> in `a^2+b^2=c^2`.
				</td>
			</tr>
			<tr>
				<td>
					<table class="pythagoras">
						<tr>
							<td>zijde</td><td>kwadraat</td><td></td>
						</tr>
						<tr>
							<td><em>AB</em> = 2</td><td>4</td><td></td>
						</tr>
						<tr>
							<td><em>AC</em> = 4</td><td>16</td><td>+</td>
						</tr>
						<tr>
							<td><em>BC</em> = ?</td><td>...</td><td></td>
						</tr>
					</table>
					<br>
					<em>BC</em> = `sqrt(16+4)&asymp;4,47`.
				</td>
				<td>
					Dus `2^2+4^2=c^2` en `c=sqrt(16+4)&asymp;4,47`.
				</td>
			</tr>
		</table>
	</p>
	<br>
	<h3>Bereken exact</h3>
	<p>
		In het voorbeeld hiervoor is `sqrt(20)` op twee decimalen afgerond. Het is dus <u>niet een precies</u> antwoord, want je hebt afgerond. Geef `sqrt(20)` als antwoord er staat "Bereken <b>exact</b>" (of liever nog `sqrt(20)=2sqrt(5)`).
	</p>
	<p>
		Exact betekent dat je <u>geen decimaal getal</u> mag geven als antwoord (dus `1/2` is goed en 0,5 niet).<br>
		Als er niets bij staat, mag je zelf kiezen. Als je niet weet op hoeveel decimalen je af moet ronden, kies dan twee decimalen.
	</p>
