	<h3>Theorie C Overstaande hoeken</h3>
	<p>
		<img src="afbeeldingen/meet_fzhoeken/overstaand_th.png" width="100 px" class="rightfloat">
		Als twee lijnen elkaar snijden, gebeurt dat onder een hoek. Hoe groot die hoek is, wordt bepaald door de richtingen van die lijnen. Daardoor weet je dat hoek &ang;<em>M</em><sub>1</sub> precies even groot is als de hoek &ang;<em>M</em><sub>3</sub> (&ang;<em>M</em><sub>1</sub> = &ang;<em>M</em><sub>3</sub>, dat spreek je uit als "hoek em-één is even groot als hoek em-drie").<br>
		Je noemt &ang;<em>M</em><sub>1</sub> en &ang;<em>M</em><sub>3</sub> <b>overstaande hoeken</b>; ze staan tegenover elkaar, als je kijkt vanuit punt <em>M</em>.
	</p>
	<p>
		Uiteraard zijn &ang;<em>M</em><sub>2</sub> en &ang;<em>M</em><sub>4</sub> ook overstaande hoeken, dus &ang;<em>M</em><sub>2</sub> = &ang;<em>M</em><sub>4</sub>.
	</p>
