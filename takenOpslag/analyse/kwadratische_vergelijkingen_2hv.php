	<div class="tekstblok theorie analyse">
		In opgave `9` heb je herleid. In opgave 10 deed je juist het omgekeerde.<br>
		Je kreeg de som en moest het als een product schrijven. Het schrijven als een product noem je <b>ontbinden in factoren</b>.
		Bij `x(7x - 3) = 7x^2 - 3x` staat `x` voor de haakjes omdat `x` in de term `7x^2` zit <b>en</b> in de term `3x`. Je noemt dit de <b>gemeenschappelijke factor</b>.
		<br>
		Bij `3x(4x + 2) = 12x^2 + 6x` staat `3x` voor de haakjes omdat `3x` in de term `12x^2` staat <b>en</b> in de term `6x`. Hier is dus `3x` de gemeenschappelijke factor.
		Voorbeeld:<br>
		<aside class="tekstballon_theorie">
			Bedenk dat `10x^2 = 2*5*x*x` en
		</aside>
		Ontbind `10x^2 + 14x` in factoren.<br>
		Stap 1:

		`2x(5x + 7)`

