	<div class="tekstblok theorie analyse">
		<h3>Oplossingen van vergelijking</h3>
			Een vergelijking in de wiskunde is een betrekking waarin twee
			uitdrukkingen (formules), waarin onbekenden (letters) voorkomen, 
			aan elkaar gelijk worden gesteld. 
			De gelijkstelling gebeurt met een gelijkheidsteken, (=), 
			zoals in de vergelijking: <br>
			<br>
			`2x+3 = 5-x`<br>
			<br>
 			Hier wordt de uitdrukking `2x + 3`, met daarin de onbekende x, gelijkgesteld aan de uitdrukking `5-x`.
 			<br>
 			De oplossing van de vergelijking is het getal dat je voor `x` moet invullen om er voor te zorgen dat er uit `2x+3` het zelfde getal komt als `5-x`.
 			Dit is de x-coordinaatvan het snijpunt van de grafieken van `y=2x+3` en `y=5-x`.<br>
 			<br>
 			<img src="afbeeldingen/analyse_kwadratische_vergelijkingen_2hv/vergelijkingen_oplossen_met_grafiek.png" class="rightfloat" width="300" />
 			Voorbeeld:<br>
 			Lees uit de figuur hiernaast de oplossingen af van de vergelijking:<br>
 			`y=2x^2+4x+8` en `y=-4x+8`.<br>
 			
 			De oplossingen van de vergelijking zijn: `x=-4` en `x=0`.
 	</div>



