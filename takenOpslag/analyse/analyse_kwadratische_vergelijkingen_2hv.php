	<div class="tekstblok theorie analyse">
		<h3>vergelijkingen van de vorm `x^2 = c`</h3>
		<p>
			Zoals je in de opgave hierboven hebt gezien heeft de vergelijking 
			`x^2 = c`:<br> <b>of</b> `2` oplossingen, <b>of</b> `1` oplossing, <b>of</b> geen oplossingen.<br>
			Bekijk het volgende schema:<br>
		</p>
			<TABLE  FRAME="void" RULES="all" style= "text-align: center">
					<caption>De vergelijking `x^2 = c`<br></caption>
					<TR >
						<TD>`c&gt0`<br>geeft twee oplossingen</TD><TD>`c=0`<br>geeft één oplossing</TD><TD>`c&lt0`<br>geeft geen oplossingen</TD>
					</TR>
					<TR>
						<TD width="500px">`x^2=9` geeft<br>`x=3` of `x=-3`<br><br><br>`x^2=10` geeft<br>`x=sqrt(10)` of `x=-sqrt(10)`<br> `x&asymp;3,162` of `x&asymp;-3,162`</TD><TD width="500px">`x^2=0` geeft<br> `x=0`</TD><TD width="500px">`x^2=-9` geeft<br>geen oplossingen<br><br><br>`x^2=-10` geeft<br>geen oplossingen</TD>
					</TR>
				</TABLE>
			<p>
				Soms moet je er eerst wat voor doen om een vergelijking van de vorm `x^2 = c` te herkennen.
				Gebruik de bordjesmethode om de vergelijking te herleiden tot je `x^2 = c` overhoudt.
				Bekijk de volgende voorbeelden:<br>
				<br>
				Voorbeeld 1:<br>
				`x^2 + 3 = 19`<br>
				`x^2 = 16`<br>
				`x = 4` of `x = -4`<br>
				<br>
				Voorbeeld 2:<br>
				`3x^2 - 8 = 67`<br>
				`3x^2 = 75`<br>
				`x^2 = 25`<br>
				`x = 5` of `x = -5`<br>
				<br>
				Voorbeeld 3:<br>
				`0,5(x + 2)^2 = 18`<br>
				`(x + 2)^2 = 36`<br>
				`x+2 = 6` of `x+2 = -6`<br>
				`x=4` of `x=-8`
			</p>
		</div>
		
