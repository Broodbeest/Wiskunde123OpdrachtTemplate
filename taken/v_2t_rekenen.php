	<h3>Theorie N Tiendelige of decimale breuken</h3>
	<p>
		Tiendelige of decimale breuken zijn breuken waarvan de noemer 10, 100, 1000 of een andere macht van 10 is.
	</p>
	<p>
		Een tiendelige breuk kun je snel schrijven als een kommagetal: 	
	</p>
	<p>
		`5/10`  is gelijk aan  `0,5`, want `5/10 = 5 : 10 = 0,5`<br>
		`2 1/10`  is gelijk aan `2,1`, want `2 1/10 = 21/10 = 21 : 10 = 2,1`<br>
		`1 78/100` is gelijk aan `1,78`, want `1 78/100 = 178/100 = 178 : 100 = 1,78`</p>
	</p>
	<p>
	 Voor de komma staan de helen, achter de komma staat het breukdeel: de teller van de decimale breuk.
	</p>

