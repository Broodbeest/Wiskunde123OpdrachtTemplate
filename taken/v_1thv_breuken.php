<div class="tekstblok theorie vaardigheden" id="theorieC">
	<h3>Theorie C Gelijknamig maken</h3>
	<p>
		Om breuken te vergelijken, op te tellen of af te trekken moeten die breuken <b>dezelfde noemer</b> hebben.<br>
		Het gelijk maken van de noemers heet <b>gelijknamig maken</b>. 
	</p>
		
		<img src="afbeeldingen/vaardigheden_leerjaar_1/blauwe_en_rode_breuk.png" alt="blauwe_en_rode_breuk" width="" height="" />
	<p>
		<u>Voorbeeld</u>:<br>
		Is de blauwe of de rode breuk groter?
	
	</p>
	<p>
		<u>Uitwerking</u>:<br>
		Maak de breuken eerst gelijknamig.<br>
		`1/5=9/45` en `2/9=10/45`.
	</p>
	<p>
		Nu zie je dat `9/45` kleiner is dan `10/45`. In wiskundetaal noteer je: `9/45` &#60; `10/45`.<br> 
		De blauwe breuk is kleiner dan de rode, dus de rode breuk is groter.
	</p>
	<p>
		Bekijk de instructieclip hieronder over het gelijknamig maken van twee breuken.
