	<h3>Theorie E Inklemmethode I</h3>
	<p>
		Een vergelijking kun je natuurlijk ook oplossen als je toevallig het juiste getal raadt.<br>
		Met inklemmen doe je iets dat daar op lijkt: je raadt het getal, zit je te hoog, dan neem je iets lagers, zit je te laag, dan iets hogers. Zo benader je het antwoord steeds meer.
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
		Juro werkt in een Chinees restaurant. De formule om zijn inkomsten uit te rekenen is:<br>
		<em>Inkomsten in €</em> `= 4t + 32`<br>
		Met `t` weer het aantal uur dat hij werkt.
	</p>
	<p>
		Bereken met inklemmen hoe lang het duurt voordat Juro 96 euro heeft verdiend.
	</p>
	<p>
		We kiezen een getal voor `t` en proberen of het klopt, anders kiezen we een ander getal.
	</p>
