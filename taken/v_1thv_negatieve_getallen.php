<div class="tekstblok theorie vaardigheden" id="theorieB">
	<h3>Theorie B Notatie</h3>
	<p>
		Een negatief getal geef je aan met behulp van een <code style="color:black">-</code>. Bij positieve getallen zouden we een <code style="color:black">+</code> kunnen gebruiken, maar we spreken af dat een getal positief is, tenzij er een <code style="color:black">-</code> voor staat.
	</p>
	<p>
		Verwar deze <code style="color:black">+</code> en <code style="color:black">-</code> van positief en negatief niet met de <code style="color:black">+</code> en <code style="color:black">-</code> van optellen en aftrekken.	</p>
</div>
	
