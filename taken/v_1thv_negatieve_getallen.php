<div class="tekstblok theorie vaardigheden" id="theorieA">
	<h3>Theorie A Groter dan of kleiner dan</h3>
	<div class="row">
		<div class="col-xs-8">
			<p>
				Een lijn met getallen erbij is een <b>getallenlijn</b>. Een thermometer is vergelijkbaar met een verticale getallenlijn.
			</p>
			<p>
				De getallen boven de nul noemen we <b>positief</b> en de getallen onder nul <b>negatief</b>. Het getal 0 is niet positief en ook niet negatief.
			</p>
			<p>
				Hoe hoger op de getallenlijn, hoe groter het getal. 5 is groter dan 4, want 5 staat hoger op de getallenlijn. In wiskundetaal schrijf je: <nobr><code style="color:black">5 &#62; 4</code>, <em>5 is groter dan 4</em>.</nobr>
			</p>
			<p>
				Getallen die lager liggen zijn kleiner dan de getallen erboven. 3 is kleiner dan 4, want 3 staat lager op de getallenlijn, in wiskundetaal: <nobr><code style="color:black">3 &#60; 4</code>, <em>3 is kleiner dan 4</em>.</nobr>
			</p>
			<p>
				Positieve getallen zijn dus altijd groter dan negatieve getallen, dus: 2 &#62; -2
			</p>
			<p>
				Negatieve getallen zijn dus altijd kleiner dan negatieve getallen, dus: -2 &#60; 2
			</p>
		</div>
		<div class="col-xs-4">
			<img src="afbeeldingen/vaardigheden_leerjaar_1/verticale_getallenlijn.png" alt="verticale_getallenlijn" height="400px !important">
