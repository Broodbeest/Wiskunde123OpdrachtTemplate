<div class="tekstblok theorie analyse" id="theorieC">
			<h3>Theorie C Coördinaten</h3>
			<p>
				Een assenstelsel zonder grootheden heet een <em>xy</em>-assenstelsel. Vaak wordt zo'n assenstelsel met stapgrootte één getekend. De <em>y</em> spreek je uit als: “ei”.
			</p>
			<div class="row">
				<div class="col-xs-7">
					<p>
						Een <b>roosterpunt</b> is het snijpunt van twee lijnen van het rooster. Een roosterpunt geef je aan met een hoofdletter, bijvoorbeeld <em>A</em> en <em>B</em>. Het snijpunt van de <em>x</em>- en <em>y</em>-as wordt de <b>oorsprong</b> genoemd. 
					</p>
					<p>
						Punten geven we aan met <b>coördinaten</b>. Deze coördinaten geven de afstand tot de oorsprong aan. 
						</p>
						<p>
						Eerst wordt de horizontale afstand gegeven, de <b><em>x</em>-coördinaat</b>, daarna de verticale afstand, de <b><em>y</em>-coördinaat</b>. Dit <b>coördinatenpaar</b> zet je tussen haakjes, direct achter de letter.
					</p>
					<p>
						Bijvoorbeeld <em>A</em>(1 , 2) en <em>B</em>(4 , 3).
					</p>
				</div>
				<div class="col-xs-5">
					<img src="afbeeldingen/analyse_leerjaar_1/theorie_C_plaatje_2.png" />
				</div>
			</div>
			<br>
			<div class="row">
				<div class="col-xs-7">
					<p>
						Het punt <em>K</em> hiernaast is geen roosterpunt. De <b><em>x</em>-coördinaat</b> van <em>K</em> is 2,5 en de <b><em>y</em>-coördinaat</b> is 4. Je krijgt dan <em>K</em>( 2,5; 4 ).
					</p>
					<p>
						Let op dat er tussen deze coördinaten geen komma, maar een <b>punt-komma</b> ( ; ) staat. Dit doe je, omdat je al een komma nodig hebt voor de decimale getallen. Wat is anders het verschil tussen ( 2,5; 4) en (2; 5,4)?
				</p>
				<p>
					Je kunt de volgorde van de coördinaten onthouden door te bedenken dat de <em>x</em> eerder in het alfabet staat dan de <em>y</em>. Of je bedenkt dat de h van horizontaal eerder in het alfabet staat dan de v van verticaal!
				</p>
				</div>
				<div class="col-xs-5">
