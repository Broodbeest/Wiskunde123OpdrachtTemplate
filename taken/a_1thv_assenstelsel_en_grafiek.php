			<h3>Theorie A Grootheden en eenheden</h3>
			<p>
				In een grafiek worden gegevens weergegeven, bijvoorbeeld de temperatuur in combinatie met de tijd. Het is belangrijk om te weten wat de getallen betekenen, daarom staat bij een <b>grootheid</b> ook de <b>eenheid</b> vermeld. 
			</p>
			<p>
				De <b>grootheid</b> is <b>wat</b> je meet. De <b>eenheid</b> is <b>waarin</b> je dat meet.
			</p>
			<p>
				Gewicht (grootheid) meet je in kilogrammen (eenheid). Temperatuur (grootheid) meet je in graden Celsius (eenheid). En als je vertelt hoe oud je bent, dan heb je het over de grootheid leeftijd in de eenheid jaren.
	</p>
