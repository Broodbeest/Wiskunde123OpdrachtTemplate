	<h3>Theorie D Tweetermen en Veeltermen</h3>
		<p>-3 <mo>&sdot;</mo><em>a</em> <mo>&sdot;</mo> 2 + <em>b</em> <mo>&sdot;</mo> 3 <mo>&sdot;</mo> 8
				is een <b>twee</b>term. De eerste term is -3 <mo>&sdot;</mo><em>a</em> <mo>&sdot;</mo> 2 en de tweede term is <em>b</em> <mo>&sdot;</mo> 3 <mo>&sdot;</mo> 8.<br>
				Je kunt deze tweeterm herleiden tot -6<em>a</em> + 24<em>b</em><br>
				<br>
				-3 <mo>&sdot;</mo><em>a</em> <mo>&sdot;</mo> 2 + <em>b</em> <mo>&sdot;</mo> 3 <mo>&sdot;</mo> 8 + 4<em>a</em> is een <b>drie</b>term. Je kan dit herleiden tot de <b>twee</b>term
				10<em>a</em> + 24<em>b</em>.<br>
				<br>
				-5 <mo>&sdot;</mo><em>p</em> <mo>&sdot;</mo> 2 + 3<em>q</em> <mo>&sdot;</mo> 3 <mo>&sdot;</mo> 8 - 2<em>r</em> <mo>&sdot;</mo> 7 is een voorbeeld van een <b>drie</b>term.<br>
				Je kunt deze drieterm herleiden tot -10<em>p</em> + 72<em>q</em> - 14<em>r</em>.<br>
				<br>
				Tweetermen, drietermen, viertermen, vijftermen enz. worden ook wel <b>veel</b>termen genoemd.<br>
