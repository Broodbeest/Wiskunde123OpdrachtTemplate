		Een korte manier van schrijven is: 3<sup>4</sup>.
		</p>
		<p>
			3<sup>4</sup> heet een <b>macht</b>.
		</p>
		<p>
		   3<sup>4</sup> en 3<sup>7</sup> zijn allebei machten van 3.<br>
		   Van beide machten is 3 het <b>grondtal</b>.<br>
		   De 4 en de 7 heten <b>exponenten</b>.
		</p>
		<p>
			<b>Let op</b>: -3<sup>4</sup> betekent <em>negatief </em>3<sup>4</sup>.<br>
			Uitspraak: "min de vierde macht van drie".<br>
			-3<sup>4</sup> = - 3 <mo>&sdot;</mo> 3 <mo>&sdot;</mo> 3 <mo>&sdot;</mo> 3 = -81.			
		</p>
		<p>
			<b>Maar</b>: (-3)<sup>4</sup> betekent grondtal -3 tot de macht 4.<br>
			Uitspraak: "de vierde macht van min drie".<br>
			(-3)<sup>4</sup> = -3 &sdot; -3 &sdot; -3 &sdot; -3 = 81.
