	<h3>Theorie D Van <b>woord</b>formule naar een formule met letters</h3>
	<p>We kijken nog even terug naar een opgave die we gemaakt hebben bij Woordformules:</p>
<div class="tekstblok opdracht">
	<h3>Opdracht 6 Schilder <em>Van Wanten</em></h3>
	<p>De woonkamer van Harry is aan een flinke opknapbeurt toe. Al zijn wanden moeten worden geschilderd. Hij kan zelf niet goed schilderen en dus vraagt hij een aantal offertes op. Schildersbedrijf <em>Van Wanten</em> geeft Harry de volgende formule op waarmee zij het bedrag berekenen:<br>
	<em>Bedrag</em> = 52 + 18 &times; <em>aantal uren</em>.
		<ol>
			<li>Hoeveel moet Harry betalen als de schilder 9 uur aan het schilderen is geweest?</li>
			<li>Wat stelt de 18 voor in de formule?</li>
			<li>Wat stelt de 52 voor in de formule?</li>
		</ol>
		<div class="hider">
			<div class="showhide">&rarr;</div>
			<div class="antwoord"><p>Antwoorden
					<ol>
						<li>52 + 18 &times; 9 = 52 + 162 = &euro;214</li>
						<li>&euro;18 per uur</li>
						<li>&euro;52 vaste kosten</li>
					</ol>
				</p>
			</div>
		</div>
	</p>
</div>

	<p>In de bovenstaande opgave werken we met de woordformule:</p>
	<p>
	<nobr><em>Bedrag</em> = 52 + 18 × <em>aantal uren</em></nobr>.
	</p>
	<p>
	Dit heet een <b>woord</b>formule omdat we in deze formule woorden gebruiken. We kunnen deze formule korter maken door geen woorden te gebruiken maar letters. In plaats van het woord <em>bedrag</em> gebruiken we de <b>letter</b> <em>b</em>, voor <em>aantal uren</em> gebruiken we de <b>letter</b> <em>u</em>.  De formule wordt dan: <nobr><em>b</em> = 52 + 18 × <em>u</em></nobr>. </p>
	<p>Tussen een getal en een letter laten we het vermenigvuldigingsteken helemaal weg: "18 × <em>u</em>" schrijven we als "18<em>u</em>".</p>
	<p>De formule die we uiteindelijk krijgen is: <nobr><em>b</em> = 52 + 18<em>u</em></nobr>.</p>
</div>

