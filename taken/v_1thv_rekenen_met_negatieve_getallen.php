	<h3>Theorie B Met de getallenlijn</h3>
	<p>
		Bovenstaande opdrachten lijken op de sommen die je vroeger al maakte. Er komt <code style="color:green">+3</code> bij of er gaat <code style="color:green">+3</code> af. Je rekent eigenlijk gewoon met positieve getallen, het grote verschil is het beginpunt.
	</p>
	<p>
		Als je ergens een <code style="color:green">positief</code> getal bij op telt, wordt je eindantwoord groter. Als je ergens een <code style="color:green">positief</code>getal van af haalt, wordt je eindantwoord kleiner. Het maakt niet uit waar je begint.
	</p>
	<p>
		<u>Voorbeeld optellen</u>:<br>
		<code style="color:green">+8</code>+<code style="color:green">+5</code> = <code style="color:green">+13</code> het eindantwoord is 5 <b>groter</b> dan het begingetal.<br>
		<code style="color:red">-2</code>+<code style="color:green">+5</code> = <code style="color:green">+3</code> het eindantwoord is ook 5 <b>groter</b> dan het begingetal.
	</p>
	<p>
		<u>Voorbeeld aftrekken</u>:<br>
		 <code style="color:">+7</code> - <code style="color:green">+4</code> = <code style="color:green">+3</code> het eindantwoord is 4 <b>kleiner</b> dan het begingetal.<br>
		 <code style="color:red">-2</code> - <code style="color:green">+4</code> = <code style="color:red">-6</code> het eindantwoord is weer 4 <b>kleiner</b> dan het begingetal. 
	</p>		
</div>

