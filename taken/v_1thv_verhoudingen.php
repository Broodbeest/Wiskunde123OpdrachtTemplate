	<h3>Theorie E Via de 1</h3>
	<p>
		Bij het rekenen met de verhoudingstabel is het vaak handig om via de 1 te gaan. Rekenen met 1 is namelijk makkelijk. Kijk naar het voorbeeld.
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
		Voor 2 liter motorolie betaal je € 8,95. Hoeveel kost 7 liter dan?
	</p>
