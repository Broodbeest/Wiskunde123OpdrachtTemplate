		<tr>
			<td width="220">
				Je rekent via 1 omdat dat makkelijk is:<br>
				van 2 liter naar 1, dus ÷ 2<br>
				van 1 liter naar 7, dus × 7.
			</td>
			<td width="20"></td>
			<td width="400"> 
