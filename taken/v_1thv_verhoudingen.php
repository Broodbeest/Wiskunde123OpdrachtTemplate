	<h3>Theorie G In verhouding (havo/vwo)</h3>
	<p>
		Je kunt ook de verhouding tussen meerdere getallen hebben.
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
			Een tegelzetter betegelt een vloer met 36 rode tegels, 18 bruine en 14 gele tegels voor een vloer.
			De verhouding van de kleuren tegels is dus 36 : 18 : 14. Ook deze verhouding kun je vereenvoudigen. 36 en 18 kun je allebei door 18, maar 14 niet...<br>
			Gebruik priemontbinding om te vereenvoudingen.</p>
	<p>Je kunt hier heel goed priemfactorontbinding toepassen.
		</p>
	<p>
			36 = 2&sdot;2&sdot;3&sdot;3.<br>
			18 = 2&sdot;3&sdot;3<br>
			14 = 2&sdot;7
		</p>
	<p>Alle drie getallen uit de verhouding 36 : 18 : 14 zijn deelbaar door 2, de vereenvoudigde verhouding is <nobr>18 : 9 : 7</nobr>.
	</p>
	<p>
		Gebruik de <a href="index2.php?p=vaar_eigenschappen_deelbaarheid">Eigenschappen van Deelbaarheid</a> die je in thema 2 leerde. Het belangrijkste staat hieronder samengevat.
	</p>
	<ul>
		<li>Als een getal even is, kun je het delen door 2<br>
			<u>Voorbeeld</u>: Het laatste cijfer van 5328 is 8 en 8 is even.<br>
			5328 = 2&sdot;2664</li><br>
		<li>Als de cijfers bij elkaar opgeteled deelbaar zijn door 3, dan is het getal ook deelbaar door 3.<br>
			<u>Voorbeeld</u>: De som van de cijfers van het getal 5328 is 18 (want 5 + 3 + 2 + 8 = 18). Achttien is deelbaar door 3, dus 5328 ook.<br>
			5328 = 3&sdot;1776</li><br>
		<li>Een getal is deelbaar door 5 als het laatste cijfer een 0 of een 5 is.<br>
			<u>Voorbeeld</u>: Het laatste cijfer van 5328 is niet een 0 of 5, dus niet deelbaar door 5.</li>
	</ul>
</div>

