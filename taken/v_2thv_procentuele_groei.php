		Een broek van &euro;`69` wordt met `8%` in prijs verhoogd.<br>
		Het gaat hier om een procentuele <b>toename</b>. `8%` erbij betekent: `100% + 8% = 108%` <br>
		De factor is dus `108/100 = 1,08`
	</p>
	<p>
		NIEUW `= 1,08 &times; 69 = 74,52`<br>
		De nieuwe prijs is &euro;`74,52`.
	</p>
	<p>
		Je berekent de nieuwe prijs met de formule:<br>
		<br>
		NIEUW = factor &times; OUD de nieuwe prijs.
	</p>
	<p>
		We noemen de factor <b>groeifactor</b>, omdat het om een procentuele <b>toename</b> gaat.
	</p>
