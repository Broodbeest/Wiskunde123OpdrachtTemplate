	<h3>Bij een gegeven afnamepercentage de nieuwe prijs uitrekenen</h3>
	<p>
		<u>Voorbeeld</u>:<br>
		Een broek van &euro;`69` wordt met `8%` in prijs verlaagd.<br>
		Bereken met de formule NIEUW = factor &times; OUD de nieuwe prijs.
	</p>
	<p>
		<u>Uitwerking</u>:<br>
		Het gaat hier om een procentuele <b>afname</b>. 
		`8%` eraf betekent: `100% - 8% = 92%` <br>
		De factor `= 92/100 = 0,92`
	</p>
	<p>
		<aside class="tekstballon_theorie">
			<p>
				Bij procentuele <b>toename</b>: groeifactor <nobr>&gt; `1`</nobr> <br>
