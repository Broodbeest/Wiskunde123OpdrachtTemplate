			</p>
		</aside>
		NIEUW `= 0,92 &times; 69 = 63,48`<br>
		De nieuwe prijs is dus &euro;`63,48`.
	</p>
	<p>
		Omdat een procentuele afname ook wel een neagtieve procentuele toename is, noemen we de factor ook <b>groeifactor</b>.
