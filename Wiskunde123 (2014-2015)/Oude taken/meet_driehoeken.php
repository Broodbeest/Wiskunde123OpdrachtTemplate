		<p>	De belangrijkste soorten driehoeken zijn de <b>rechthoekige driehoek</b>, de <b>gelijkbenige driehoek</b> en de <b>gelijkzijdige driehoek</b>. Deze drie driehoeken kun jij construeren. Vanwege het construeren heb je bij sommige opdrachten de basisconstructies weer nodig. Mocht je niet meer helemaal weten hoe het ook alweer allemaal moest, pak dan je eigen samenvatting erbij uit de taken <a href="index2.php?p=meetkunde_arabesk" target="_blank">Arabesk</a> en <a href="index2.php?p=meetkunde_origami" target="_blank">Origami</a>.</p>
