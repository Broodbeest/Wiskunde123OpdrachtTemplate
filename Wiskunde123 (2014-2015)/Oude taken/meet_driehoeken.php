		<p>
			<ol>
				<li>Construeer een rechthoekige driehoek <em>ABC</em>, waarvan &ang;<em>A</em> recht is en <nobr><em>AB</em> > <em>AC</em></nobr>.<br>
					<nobr><em>AB</em> > <em>AC</em></nobr> spreek je uit als: "(lijnstuk) aa-be is groter dan aa-ce".</li>
