		<h3>Theorie B Gelijkbenige driehoek</h3>
		<p><b>Gelijkbenige driehoeken</b> hebben twee belangrijke eigenschappen
			<ul>
				<li>twee van de lijnstukken waaruit de driehoek bestaat zijn even lang én </li>
				<li>tweee van de hoeken in de driehoek zijn even groot.</li>
			</ul>
			<img src="afbeeldingen/meetkunde_driehoeken/th_tophoek.png" width="100px" class="rightfloat">
			Er zijn in een gelijkbenige driehoek dus <u>altijd</u> twee hoeken even groot. De derde hoek in de gelijkbenige driehoek wordt de <b>tophoek</b> genoemd.<br>
			Misschien kun je het zo goed onthouden: Een mens heeft ook twee benen die (als het goed is) precies even lang zijn. Of je nu een spagaat maakt, of met je benen naast elkaar staat, de hoeken die je benen met de grond maken zijn aan elkaar gelijk. En meestal is je hoofd het hoogste punt, dus de derde hoek noem je de tophoek.
		</p>
