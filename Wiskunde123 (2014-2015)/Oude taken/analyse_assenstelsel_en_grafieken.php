	<div class="tekstblok theorie analyse">
			<h3>Theorie C Samenvatting assenstel</h3>
			<p>Een assenstelsel teken je op roosterpapier. De horizontale lijn noemen we de horizontale as, of korter: de <b><em>x</em>-as</b>. De verticale lijn noemen we de verticale as, of korter: de <b><em>y</em>-as</b>. Waar de assen elkaar snijden heet de oorsprong en geef je aan met de hoofdletter <em>O</em>.</p>
			<p>Langs de assen staan getallen. De <em>x</em>-as, de <em>y</em>-as en de oorsprong vormen samen het assenstelsel.</p>
			<p>In het assenstelsel staan punten. Zoals je bij meetkunde geleerd hebt, noteer je punten altijd met een hoofdletter. Om bij een punt in het assenstelsel te komen begin je altijd in de oorsprong. Je loopt altijd eerst naar rechts en dan omhoog.<img src="afbeeldingen/analyse_1/th_punt.png" width="250" height class="rightfloat"></p>
			<p>Dus om punt <em>A</em>(4, 2) te tekenen, begin je met je potloodpunt in de oorsprong en gaat eerst 4 stappen over de <em>x</em>-as naar rechts en daarna 2 stapjes in verticale richting. Zie het plaatje hiernaast.</p>
			<p>Die getallen binnen de haakjes noemen we coördinaten. Het eerste getal is de <em>x</em>-coördinaat en het tweede getal is de <em>y</em>-coördinaat.</p>
			<p>Je kan de volgorde makkelijk onthouden door te bedenken dat de x van <em>x</em>-coördinaat eerder in het alfabet staat dan de y van <em>y</em>-coördinaat. Of je bedenkt dat de h van horizontaal eerder in het alfabet staat dan de v van verticaal!</p>
			<p><img src="afbeeldingen/analyse_1/descartes1.png" width="250" height class="rightfloat"/>De eerste die het assenstelsel gebruikte om de plaats van een punt aan te geven was <a href="http://nl.wikipedia.org/wiki/Ren%C3%A9_Descartes">René Descartes (1596-1650)</a>. Dit was een Franse geleerde die ook in Nederland heeft gestudeerd. Men zegt dat hij op het idee van een assenstelsel kwam door een vlieg die in zijn kamer vloog. Hij bedacht dat de plaats van de vlieg op het plafond steeds te beschrijven is door uit te gaan van een vast punt, de oorsprong.</p>
