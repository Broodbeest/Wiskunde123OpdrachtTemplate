			<h3 class="titel vaardigheden">Theorie D Kgv via priemfactorontbinding</h3>
			<p>
				Bovenstaande methode is veel werk bij grote getallen. Handiger is dan om de getallen te schrijven als een <a href="http://wiskunde.vathorstcollege.nl/index2.php?p=ontbinden_in_priemfactoren" target="_blank">ontbinding van priemfactoren</a>.  
			</p>
			<p>
				<u>Voorbeeld</u>:<br>
				Bepaal de kgv van 6 en 10.
			</p>
			<p>
				Uitwerking:<br>
				<ul id="numbers">
					<li>
						Ontbind beide getallen in priemfactoren.<br>
						6= 2 × 3<br>
						10 = 2 × 5<br>
						<br>
					</li>
					<li>
						Neem de priemfactorontbinding van het grootste getal:<br>
						2 × 5<br>
						<br>
					</li>
					<li>
						Kijk welke factoren uit de priemfactorontbinding van het andere 		getal je nog niet hebt gebruikt:<br>
						3<br>
						<br>
					</li>
					<li>
						Vermenigvuldig de gevonden factoren met elkaar:<br>
						2 × 5 × 3 = 30<br>
						Dus	kgv(6, 10) = 30<br>
					</li>
				</ul>	
				<p>
					Gebruik een ladderdiagram om getallen in priemfactoren te ontbinden.
				</p>	
		</div>
				

