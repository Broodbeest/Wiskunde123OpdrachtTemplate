		<div class="tekstblok theorie vaardigheden">
			<h3 class="titel vaardigheden">Theorie A De grootste gemene deler</h3>
			<p>
				De <b>grootste gemene deler</b>, afgekort: "ggd", is het grootste getal waardoor je twee getallen kunt delen. Je bepaalt dit getal met behulp van de gemeenschappelijke priemfactoren.
			</p>
			
			<p>
				<u>Voorbeeld</u>:<br>
				Bepaal de grootste gemene deler van 120 en 585. 
			</p>
			<p>
			Uitwerking:
			<ul id="numbers">
				<li><img src="afbeeldingen/grootste_gemene_deler/theorie_a.png" alt="theorie_a" width="150" class="rightfloat"/>Ontbind beide getallen in <a href="http://wiskunde.vathorstcollege.nl/index2.php?p=ontbinden_in_priemfactoren" target="_blank">priemfactoren</a>. Gebruik bij voorkeur een ontbindingsboom, zie voorbeeld.<br>
					120 = 2 × 2 × 2 × 3 × 5<br>
					585 = 3 × 3 × 5 × 13<br>
					<br>
				</li>
				<li>
					Bepaal de gemeenschappelijke priemfactoren.<br>
					120 = 2 × 2 × 2 × <u>3</u> × <u>5</u><br>
					585 = <u>3</u> × 3 × <u>5</u> × 13<br>
					Ze hebben 3 en 5 gemeenschappelijk.<br>
					<br>
				</li>
				<li>
					Vermenigvuldig die gemeenschappelijke delers.<br>
					3 × 5 = 15<br>
					15 is de grootste gemene deler, want 120 en 585 kunnen allebei door 15 worden gedeeld.<br>
					En er bestaat géén groter getal waardoor 120 en 585 gedeeld kunnen worden.<br>
					<br>
				</li>
				<li>
					Controleer je antwoord.<br>
					120 : 15 = 8<br>
					585 : 15 = 39<br>
					
			</ul>
			<p>
				Als 8 en 39 geen gemeenschappelijke delers hebben, ben je klaar.	
			</p>
		</div>
		
