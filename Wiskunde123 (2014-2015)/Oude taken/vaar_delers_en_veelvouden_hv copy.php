			<h3 class="titel vaardigheden">Theorie C Kleinste gemene veelvoud<img src="afbeeldingen/kleinste_gemene_veelvoud/getallen.jpg" alt="getallen" width="170" height="" class="rightfloat" /></h3>
			<p>
				Veelvouden kwam je al tegen bij de tafels. Zo is de tafel van 3 eigenlijk niets anders dan een opsomming van de eerste tien veelvouden van 3.
			</p>
			<p>
			<u>Voorbeeld</u>:<br>
			Veelvouden van 3 zijn: 6, 9, 12, 15 18, 21, 24, ... , et cetera.<br>
			Veelvouden van 4 zijn: 8, 12, 16, 20, 24, 28, 32, ... , et cetera.
			</p>
			<p>
				Wat is nu het <b>kleinste gemene</b> (lees: gemeenschappelijke) <b>veelvoud</b> van 3 en 4? Als je hierboven kijkt zijn de gemeenschappelijke veelvouden: 12, 24, 36, ... et cetera. Het kleinste is dan dus 12. 
			</p>
			<p>
				We noteren het als volgt: kgv(3, 4) = 12
			</p>
			<p>
				Dit lijkt op de notatie van de <a href="http://wiskunde.vathorstcollege.nl/index2.php?p=grootste_gemene_deler" target="_blank">grootste gemene deler</a>. Het kleinste gemene veelvoud is als het ware "de tegenhanger" van de grootste gemene deler. 
			</p>
