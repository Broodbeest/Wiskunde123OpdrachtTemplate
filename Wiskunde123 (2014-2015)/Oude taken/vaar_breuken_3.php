	<h3 class="titel vaardigheden">Theorie D Vermenigvuldigen van twee breuken met ‘wegstrepen’</h3>
	<p>
		Als je twee breuken vermenigvuldigt, kun je door <b>'weg te strepen'</b> heel handig rekenen. 
	</p>
	<p>
		Bekijk de instructievideo <em>Vermenigvuldigen van twee breuken met ‘wegstrepen’</em>.
	</p>
