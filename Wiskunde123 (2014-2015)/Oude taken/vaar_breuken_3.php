<h1>Rekenen met breuken 3</h1>

<div class="tekstblok theorie vaardigheden">
	<h3 class="titel vaardigheden">Inleiding</h3>
	<p>
		In de taak <em>Rekenen met breuken 2</em> heb je geleerd hoe je (gemengde) breuken kunt optellen en aftrekken. 	In deze taak behandelen we eerst het vermenigvuldigen, daarna het delen met breuken.
	</p>
	<p>
		Je leert dat je bij vermenigvuldigen de tellers en de noemers van breuken met elkaar moet vermenigvuldigen. Een gemengde breuk moet je eerst als gewone breuk herschrijven. Als je eenmaal kunt vermenigvuldigen is het delen met breuken makkelijk.
	</p>
		<p class="breuken">
		<u>Voorbeeld vermenigvuldigen</u>:
		`1 1/3*1/5=4/3*1/5=4/15`				
	</p>
	<h3>Doelen</h3>
	<ul>
		<li>
			breuken vermenigvuldigen;
		</li>
		<li>
		 breuken delen.
		</li>
	</ul>
	<h3>Werkwijzer</h3>
	<p>
		 In deze taak maken we weer gebruik van de websites <a href="http://www.sommenfabriek.nl" target="_blank">www.sommenfabriek.nl</a> en <a href="www.rekenenmetbreuken">www.rekenenmetbreuken.nl</a>. Als je de opdrachten van deze taak niet goed begrijpt, kun je daar extra uitleg en oefening vinden. Lees de theoriestukken, bekijk de instructievideo's en maak de opdrachten om je de stof eigen te maken. Succes.
