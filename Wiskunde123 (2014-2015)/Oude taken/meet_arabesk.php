	<div class="tekstblok theorie meetkunde">
		<h3><a id="theorie_a">Theorie A Basisconstructies</a></h3>
		<p>In de opdracht hiervoor heb je van een bloem een ander figuur gemaakt, door lijnstukken te trekken.</p>
		<p>Een <b>lijnstuk</b> is de kortste afstand tussen twee punten. Een lijnstuk heeft dus een beginpunt en een eindpunt. Het begin- en eindpunt zijn inwisselbaar.<br>
		Een lijnstuk geef je aan met de punten waartussen die loopt, bijvoorbeeld <b>lijnstuk <em>PQ</em></b>, ook als <em>Q</em> eigenlijk het beginpunt is, noem je het lijnstuk <em>PQ</em> (alphabetisch).</p>
		<img src="afbeeldingen/meetkunde_arabesk/th_gelijkelengte.png" width="100px" class="rightfloat">
		<p>Als twee lijnstukken precies even lang zijn, geef je dat aan door streepjes te zetten op de lijnstukken met gelijke lengte. In de afbeelding hiernaast zijn <em>AB</em> en <em>CD</em> even lang.</p>
		<p>Een <b>lijn</b> heeft <u>geen</u> begin- of eindpunt en is het verlengde van een lijnstuk. Een lijn gaat in twee richtingen oneindig lang door.<br>Een lijn geef je aan met een kleine letter, bijvoorbeeld <b>lijn <em>k</em></b>.</p>
		
		<img src="afbeeldingen/meetkunde_arabesk/theorie_a_lijn_en_lijnstuk.png" alt="theorie_a_lijn_en_lijnstuk" width="500" height="" /><figcaption>Hier zie je lijnstuk <em>PQ</em> op lijn <em>k</em>.</figcaption>
		
		<p>Lijnen en lijnstukken zijn <u>altijd</u> recht. Als je een niet rechte streep maakt, noem je dat een kromme en dus niet lijn.</p>
		<p>Nu je ook lijnen kunt trekken, ga je enkele basisconstructies leren. Deze basisconstructies zijn:
			<ul>
				<li>middellijn construeren;</li>
				<li>middenpunt construeren;</li>
				<li>loodlijnen oprichten;</li>
				<li>loodlijn neerlaten.</li>
			</ul>
			Al deze constructies ga je in het net construeren op het werkblad en bewaar je in je map, maar je gaat eerst oefenen op een oefenvel.
		</p>
	</div>
	
