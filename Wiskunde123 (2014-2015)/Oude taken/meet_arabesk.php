	<div class="tekstblok opdracht">
		<h3><a id="opdracht_5">Opdracht 5 Loodlijn neerlaten</a></h3>
		<p>Als er een lijn en een punt dat niet op die lijn ligt gegeven zijn, kun je een loodlijn maken die door dat punt gaat. Dat heet een <b>loodlijn neerlaten</b>. Hieronder vind je de stappen die je kunt nemen om de middellijn te construeren. Neem die stappen op een oefenvel.</p>
			<ol id="numbers">
				<li>trek een willekeurige lijn <em>n</em>; </li>
				<li>zet een punt <em>P</em> ergens boven (of onder) lijn <em>n</em>;</li>
				<li>trek een cirkel met middelpunt <em>P</em>. Zorg dat je de lijn <em>n</em> twee keer snijdt.<br>
					Noem de snijpunten <em>Q</em> en <em>R</em>;</li>
				<li>stel je passer in op een straal die iets groter is dan de helft van <em>QR</em>;</li>
				<li>teken twee cirkels, één met middelpunt <em>Q</em> en één met middelpunt <em>R</em>;</li>
				<li>verbind de snijpunten van de twee cirkels met een lijn. Noem die lijn <em>l</em>.<br>
					Je hebt de nu loodlijn <em>l</em> op <em>n</em> gevonden die door <em>P</em> gaat;</li>
				<li>geef de loodlijn een kleur en zet het symbool voor loodrecht in de constructie.</li>
			</ol>
		<p>Oefen het neerlaten van een loodlijn een paar keer voordat je naar de volgende opdracht gaat.</p>
	</div>
	
	<!-- opdracht 6 -->
	<div class="tekstblok opdracht">
		<h3><a id="opdracht_6">Opdracht 6 Loodlijn oprichten</a></h3>
		<p>Als gegeven is een lijn <em>k</em> met daarop een punt <em>P</em>, kun je een loodlijn <em>k</em> maken die door <em>P</em> gaat. Dat heet een <b>loodlijn oprichten</b>.
			<br>Hieronder vind je de stappen die je kunt nemen om een loodlijn op te richten. Neem die stappen op een oefenvel.
			<ol id="numbers">
				<li>trek een willekeurig lijn <em>k</em>;</li>
				<li>zet een punt <em>P</em> ergens op lijn <em>k</em>;</li>
				<li>cirkel om vanuit <em>P</em> met een straal gelijk aan de lengte van je pink;</li>
				<li>de cirkel en de lijn <em>k</em> snijden elkaar in twee snijpunten, noem die punten <em>Q</em> en <em>R</em>;</li>
				<li>neem een straal op je passer iets groter dan de lengte <em>PQ</em>, en passer om vanuit <em>Q</em> en <em>R</em>;</li>
				<li>verbind de snijpunten van de twee cirkels en je hebt de loodlijn <em>l</em> gevonden;</li>
				<li>geef de loodlijn een kleur en zet de juiste symbolen en namen in de constructie.</li>
			</ol>
		</p>
	</div>

