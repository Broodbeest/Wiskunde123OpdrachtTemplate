		<h3><a id="theorie_c">Theorie C Loodlijn neerlaten</a></h3>
		<p>Een <b>loodlijn</b> is een lijn die <b>loodrecht</b> op een andere lijn of lijnstuk staat. Twee lijnen die loodrecht op elkaar staan, delen een vlak of ruimte op in vier gelijke delen.</p>
		<img src="afbeeldingen/meetkunde_arabesk/theorie_b_loodlijnlood_met_muur.png" alt="theorie_b_loodlijnlood_met_muur" width="200" height="" class="rightfloat">
		<p>Vroeger, toen er nog niet heel geavanceerde apparatuur bestond, moesten muren van huizen ook mooi rechtop staan. Men gebruikte een stuk touw met een loden gewicht eraan om te bepalen wanneer iets recht stond; het touw hing dan recht naar beneden en zo zorgde je dat de muur een loodrechte hoek maakte met het aardoppervlak. Daar komt het woord <em>loodrecht</em> vandaan. In de geometrie gebruik je dat woord loodrecht niet alleen als je een verticale en een horizontale lijn hebt, maar altijd als twee lijnen een vlak in vier gelijke delen verdeelt.</p>
		<table>
			<tr>
				<td>Als een lijn of lijnstuk loodrecht op een andere lijn of lijnstuk staat, geven we dat aan met een speciaal <b>symbool</b>. Op de afbeelding hiernaast zie je waar het rode lijnstuk het zwarte lijnstuk snijdt twee kleine streepjes. Die kleine streepjes geven aan dat de lijnstukken loodrecht op elkaar staan.</td>
				<td><img src="afbeeldingen/meetkunde_arabesk/Loodlijn_lijn_voorbeeld_transparant.png" alt="Loodlijn_lijn_voorbeeld_transparant" width="200" height="" /></td>
