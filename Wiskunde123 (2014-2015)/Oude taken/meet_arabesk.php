					</div>
				</div></li>
				<li>Leg uit waarom je niet noodzakelijk drie keer een cirkel had hoeven te trekken rond <em>P</em> en <em>Q</em>.<br>
				<div class="hider">
					<div class="showhide">&rarr;</div>
