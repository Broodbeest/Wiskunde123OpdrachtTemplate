		<p>Eén lijn is wel heel speciaal; de <b>loodlijn</b>. Deze lijn is zo speciaal dat je hem overal tegen komt in het dagelijks leven. Deze lijn is ook heel belangrijk in de geometrie.</p>
		<p>In deze taak leer je vier <b>basisconstructies</b>. Met deze constructies kun je veel wiskundige figuren maken. De taak sluit je af met het maken van een Islamitische arabesk. Als je maar genoeg van die arabesken tegen elkaar aan legt, krijg je een heel mooie muur, zoals te zien op het plaatje.</p>
