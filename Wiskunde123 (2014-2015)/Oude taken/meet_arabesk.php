		<h3><a id="theorie_b">Theorie B Wiskundige notatie</a></h3>
		<p>In de taak Bloem heb je al geleerd dat er binnen de wiskunde vaak afspraken worden gemaakt. Zo noteer je een lijn altijd met een kleine letter en een punt altijd met een hoofdletter. Met <em>PQ</em> bedoelen we een lijnstuk dat loopt van punt <em>P</em> tot en met punt <em>Q</em>, het lijnstuk <em>QP</em> is natuurlijk hetzelfde lijnstuk, toch proberen we altijd alfabetisch te werken want dan kun je in één oogopslag zien welk lijnstuk je bedoelt.</p>
		<p>Zo is er ook een afspraak om een cirkel om een punt <em>P</em> met een straal met lengte <em>QR</em> als volgt te benoemen:<br>
			Omcirkel punt <em>P</em> met straal <em>QR</em>.</p>
		<p>Dat is iets korter dan zeggen:<br>
			Trek een cirkel met middelpunt <em>P</em> en met straal <em>QR</em>.</p>
