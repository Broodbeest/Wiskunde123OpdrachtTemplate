		<h3><a id="opdracht_2">Opdracht 2 Middellijn</a></h3>
		<p>Een middellijn van <em>P</em> en <em>Q</em> is de verzameling van punten die even ver van <em>P</em> als van <em>Q</em> af liggen.</p>
		<p>Hieronder vind je de stappen die je kunt nemen om de middellijn te construeren. Voer de stappen op een oefenvel uit.
			<ol id="numbers">
				<li>Zet twee punten, <em>P</em> en <em>Q</em> op een leeg vel. Zorg dat de punten ongeveer de lengte van je pink uit elkaar liggen.</li>
				<li>Trek een cirkel met middelpunt <em>P</em> en een straal zo lang als je ringvinger. Trek ook om <em>Q</em> een cirkel met dezelfde straal. Markeer de snijpunten van deze twee cirkels;</li>
				<li>Trek weer cirkels om <em>P</em> en <em>Q</em>, maar nu met een straal zo lang als de afstand tussen je duim en wijsvinger en markeer de snijpunten van deze twee cirkels;</li>
				<li>Trek nog een keer cirkels om <em>P</em> en <em>Q</em>, nu met een straal zo lang als de afstand tussen je duim en pink. Markeer de snijpunten van deze twee cirkels;</li>
				<li>Verbind alle snijpunten met elkaar;</li>
				<li>Noem de ontstane lijn <em>m</em>. Lijn <em>m</em> is de gezochte middenlijn</li>
			</ol>
			</p><p>Beantwoord de volgende twee vragen:
			<ol>
				<li>Leg uit waarom lijn <em>m</em> de middellijn heet.</li>
				<li>(Extra) Als je een bloem met twaalf bladen maakt, gebruik je ook een middellijn. Beschrijf dat moment.<br>
				<div class="hider">
					<div class="showhide">&rarr;</div>
