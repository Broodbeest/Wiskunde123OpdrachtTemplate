		<h3><a id="theorie_d">Theorie D Wiskunde en praktijk</a></h3>
		<p>In de wiskunde kom je loodlijnen nog vaak tegen, bijvoorbeeld bij <b>hoogtelijnen</b> (thema 6). Hoogtelijnen heb je nodig om de oppervlakte van een driehoek te bepalen.</p>
