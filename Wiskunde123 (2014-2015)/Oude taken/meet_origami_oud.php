<div class="tekstblok theorie meetkunde">
	<h3><a id="theorie_b">Theorie B Gelijke lengte</a><img src="afbeeldingen/meetkunde_origami/evenlang_notatie.png" width="100 px" class="rightfloat"></h3>
	<p>In de opdracht hiervoor heb je gemerkt dat er ook <b>notatie</b> bestaat voor gelijke lengte.<br>
		De notatie voor twee lijnstukken met dezelfde lengte is dat je in elk lijnstuk markeert met een <b>streepje</b>. Zijn er twee paar lijnstukken met gelijke lengte, geef je het eerste paar één streepje en het tweede paar twee streepjes, enzovoort (zie de figuur).</p>
	<p>Als jij iets construeert, pas je de bijpassende notatie toe, zodat iemand die jouw werk ziet <b>zeker weet</b> dat twee lijnstukken even lang zijn (ook als dat misschien niet zo lijkt), of dat twee lijnen loodrecht zijn, enzovoort.</p>
</div>

