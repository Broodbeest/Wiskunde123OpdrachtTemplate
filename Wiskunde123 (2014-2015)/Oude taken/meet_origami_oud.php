	<h3>Theorie C Evenwijdige lijnen</a></h3>
	<p>Als twee lijnen steeds op gelijke afstand van elkaar liggen, hebben ze dezelfde richting. Je spreekt dan van <b>evenwijdige lijnen</b>. Als lijnen niet evenwijdig zijn, snijden ze elkaar in een punt: het snijpunt.</p>
	<img src="afbeeldingen/meetkunde_origami/evenwijdig_notatie.png" width="400 px">
	<p>Als twee lijnen evenwijdig zijn, geef je dat in je constructie aan door op beide lijnen een <b>pijltje</b> te tekenen (zie de afbeelding hierboven). Zijn er in je tekening meerdere lijnen die evenwijdig zijn, geef je het eerste paar evenwijdige lijnen één pijltje, en het tweede paar twee pijltjes, enzovoort.</p>
</div>

