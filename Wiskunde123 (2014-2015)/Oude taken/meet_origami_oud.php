	<h3><a id="opdracht_5">Extra Opdracht 5 Overbrengen van een hoek</a></h3>
	<p>In deze opdracht leer je hoe je een hoek op een andere plek na kan maken; het <b>overbrengen van een hoek</b> Je gaat dat pas in de taak <a href="index2.php?p=meetkunde_fzhoeken" target="_blank">F- en Z-hoeken</a> gebruik van maken, dus zorg dat je er een goede samenvatting van maakt.</p>
	<p>
		<ol>
			<li>Doe de stappen hieronder na op een leeg vel papier.</li>
			<ol id="numbers">
				<li>maak een willekeurige hoek door de lijnen <em>l</em> en <em>k</em> elkaar te laten snijden, noem het snijpunt <em>P</em>;</li>
				<li>trek een willekeurige lijn <em>m</em> (dit is de kopie van lijn <em>l</em>);</li>
				<li>zet een willekeurig punt <em>Q</em> op lijn <em>m</em> (dit is de kopie van punt <em>P</em>);</li>
				<li>trek vanuit zowel <em>P</em> als <em>Q</em> een cirkel met dezelfde straal;</li>
				<li>noem het snijpunt van de cirkel vanuit <em>P</em> met lijn <em>k</em> punt <em>S</em><sub>1</sub> en het snijpunt van de cirkel vanuit <em>P</em> met lijn <em>l</em> punt <em>S</em><sub>2</sub>;</li>
				<li>noem het snijpunt van de cirkel vanuit <em>Q</em> met lijn <em>m</em> punt <em>S</em><sub>3</sub>;</li>
				<li>trek een cirkel met als middelpunt <em>S</em><sub>2</sub> die door punt <em>S</em><sub>1</sub> gaat en trek een cirkel met dezelfde straal vanuit <em>S</em><sub>3</sub>;</li>
				<li>noem het snijpunt van deze cirkel met de cirkel vanuit <em>Q</em> <em>S</em><sub>4</sub>;</li>
				<li>verbind <em>Q</em> met <em>S</em><sub>4</sub> en je hebt de hoek overgebracht;</li>
				<li>geef de lijnen en hoeken kleur en naam en zet de juiste symbolen in de constructie.</li>
			</ol>
			<br>
			<li>Maak een heldere samenvatting voor jezelf over hoe je een hoek moet overbrengen. Bewaar de samenvatting bij je samenvatting van de andere basisconstructies (middellijn, loodlijn, ...).</li>
