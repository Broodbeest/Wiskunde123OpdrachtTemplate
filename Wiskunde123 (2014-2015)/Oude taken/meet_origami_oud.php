<!-- theorie A -->
<div class="tekstblok theorie meetkunde">
	<h3><a id="theorie_a">Theorie A Bissectrice/deellijn</a><img src="afbeeldingen/meetkunde_origami/bissectrice_notatie.png" width="200 px" class="rightfloat"></h3>
	<p>Wat je hiervoor gemaakt hebt heet <b>deellijn</b>, maar wordt ook wel een <b>bissectrice</b> genoemd. Deze lijn deelt de ruimte tussen twee lijnen in precies twee even grote gebieden.</p>
	<p>Je construeert hem op de volgende manier (zie bewegende plaatje hiernaast):
		<ol id="numbers">
			<li>trek twee elkaar snijdende lijnen <em>m</em> en <em>n</em>;</li>
			<li>noem het snijpunt punt <em>P</em>, de <b>benen</b> <em>m</em> en <em>n</em> maken een <b>hoek</b> in punt <em>P</em>;</li>
			<li>maak een cirkel met <em>P</em> als middelpunt;<img src="afbeeldingen/meetkunde_origami/bisectie_constructie.gif" width="200px" class="rightfloat"></li>
			<li>de cirkel snijdt beide lijnen, noem deze snijpunten <em>S</em><sub>1</sub> en <em>S</em><sub>2</sub>;</li>
			<li>maak twee cirkels met gelijke straal vanuit <em>S</em><sub>1</sub> en <em>S</em><sub>2</sub>;</li>
			<li>deze twee cirkels snijden elkaar in punt <em>S</em><sub>3</sub>;</li>
			<li>verbind <em>P</em> met <em>S</em><sub>3</sub> en je hebt de bissectrice <em>b</em> gevonden;</li>
			<li>markeer de nieuwe hoeken die zijn onstaan door in de gelijke ruimtes er een gelijk <b>symbool</b> te zetten, zie de figuur hierboven.</li>
		</ol>
		In plaats van het driehoekje had je ook een ander symbool mogen gebruiken (een hartje, het symbool voor schoppen in een kaartspel...), zolang de symbolen maar gelijk aan elkaar zijn.<br>
		In wiskundige woorden zeg je dat &ang;<em>P</em><sub>1</sub> = &ang;<em>P</em><sub>2</sub> = symbool. Dat spreek je uit als: "hoek pe-één is even groot als hoek pe-twee".</p>
		<p>In de constructie hierboven zijn er twee snijpunten, dus worden alle twee die punten <em>S</em> genoemd. Om verwarring te voorkomen worden ze <em>S</em><sub>1</sub> en <em>S</em><sub>2</sub> genoemd. Je spreekt "Punt <em>S</em><sub>1</sub>" uit als "punt es-één".</p>
</div>

