<div class="tekstblok theorie vaardigheden">
	<h3>Theorie A Helen uit de breuk halen</h3>
	<p>
		Een <b>gemengde</b> of samengestelde breuk is een breuk met <b>helen</b> ervoor, bijvoorbeeld: `3 4/7`. <br> Bij een gewone of enkelvoudige breuk staan er geen helen voor de teller en noemer, bijvoorbeeld: `3/5`.
	</p>
	<p>
		Bij een onechte breuk is de teller groter dan de noemer, bijvoorbeeld `29/9`. Je kunt dan helen <b>uit</b> de breuk halen. Neem de uitleg hieronder door. 
		<img src="../afbeeldingen/rekenen_met_breuken_1/breuken-helen%20eruit.jpg" alt="breuken-helen%20eruit" width="620" height="" />
	</p>
	<p>
		Natuurlijk zou je `1 4/6` nog moeten vereenvoudigen tot `1 2/3`, maar daarover meer bij het onderdeel <b>vereenvoudigen</b>.
	</p>
	<p>
		Bekijk de instructieclip <em>Van breuk naar gemengde breuk</em>.
		<iframe width="640" height="360" src="//www.youtube.com/embed/iz3M95pstzY?feature=player_detailpage" frameborder="0" allowfullscreen></iframe>
