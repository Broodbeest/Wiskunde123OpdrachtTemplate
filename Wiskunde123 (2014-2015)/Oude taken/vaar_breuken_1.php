	<h3>Theorie C Gelijknamig maken</h3>
	<p>
		Als je breuken met elkaar wilt vergelijken, moeten die breuken <b>dezelfde noemer</b> hebben. Het gelijk maken van de noemers heet <b>gelijknamig maken</b>. Ook voor het optellen en aftrekken van breuken heb je gelijknamige breuken nodig.
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
		Welke breuk is groter, de blauwe breuk van `1/5` of de rode breuk van `2/9` ?
		<img src="../afbeeldingen/rekenen_met_breuken_1/blauwe_en_rode_breuk.png" alt="blauwe_en_rode_breuk" width="620" height="" />
	</p>
	<p>
		<u>Uitwerking</u>:<br>
		Maak de breuken eerst gelijknamig. `1/5=9/45` en `2/9=10/45`.
	</p>
	<p>
		Nu zie je dat `9/45` kleiner is dan `10/45`. In wiskundetaal noteer je: `9/45` &#60; `10/45`.<br> 
		De blauwe breuk is kleiner dan de rode.
	</p>
	<p>
		Bekijk de instructieclip hieronder over het gelijknamig maken van twee breuken.
