<div class="tekstblok theorie vaardigheden">
	<h3>Theorie D Vereenvoudigen</h3>
	<p>
		Je schrijft breuken altijd zo <b>eenvoudig</b> mogelijk op. Maak de noemer zo klein mogelijk en haal de helen eruit. <br>
		Door teller en noemer door hetzelfde getal te delen, houd je de breuk gelijk.
	</p>
	<p>
		<u>Voorbeeld 1</u>:
		<math display="inline">
			<mfrac>
				<mrow><mi>3</mi></mrow> <!--teller van de breuk-->
				<mrow><mi>9</mi></mrow> <!--noemer van de breuk-->	
			</mfrac>
