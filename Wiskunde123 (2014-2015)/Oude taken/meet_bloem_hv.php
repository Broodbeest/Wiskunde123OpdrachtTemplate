	<h3>Theorie A Inleiding</h3>
	<p>
	Meetkunde is het deel van de wiskunde dat gaat over <b>figuren</b>, zowel platte als ruimtelijke figuren. De meetkunde van deze cursus gaat alleen over de platte figuren en wordt <b>geometrie</b> genoemd. Uitgangspunt is dat alle figuren kunnen worden opgebouwd uit twee basis-elementen: de punt en de lijn. Dit opbouwen noemen we <b>construeren</b>.
	</p>
	
	<h3>Punten, lijnen en cirkels</h3>
	
	<table>
		<tbody>
			<tr>
				<td>
					<p>
					Een <b>punt</b> is de plek waar twee lijnen elkaar snijden. Een punt heeft geen afmetingen, het neemt geen ruimte in en heeft geen diepte. De beroemde wiskundige Euclides omschreef de punt als "dat wat geen delen heeft". Een zichtbare punt op papier is enkel een hulpmiddel om iets te illustreren. Een punt geef je aan met een <b>hoofdletter</b>.
					</p>
