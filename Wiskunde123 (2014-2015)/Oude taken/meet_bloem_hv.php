Definitie van lijn, punt en vlak
Punt, lijn, en vlak zijn drie belangrijke begrippen van de meetkunde. Het is dus belangrijk dat iedereen daar dezelfde voorstelling bij heeft.

Bij een punt denken we aan een heel kleine stip, kleiner dan je je kunt voorstellen. Eigenlijk is iedere stip die je tekent alweer te groot.

Bij een lijn denken we aan iets dat altijd maar doorgaat, kaarsrecht is en dunner dan dun. Je kunt ook aan een scherpe vouw in papier denken, maar dan veel langer: naar beide kanten onbegrensd. Een lijn is door twee punten helemaal vastgelegd.

Een vlak is zoiets als een tafelblad: helemaal glad, zonder enige oneffenheid, en dan in alle richtingen oneindig groot, onbegrensd.

We kunnen nu deze drie begrippen gebruiken om andere begrippen heel precies te beschrijven. Zo'n precieze uitleg noemen we een definitie. Definitie betekent: "Samenvattende omschrijving van de kenmerken van een begrip, zodat het niet met een ander verward kan worden.". (Uit: Van Dale, Groot Woordenboek der Nederlandse Taal, twaalfde herziene druk.)

