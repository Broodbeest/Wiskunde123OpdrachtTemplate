	<p>Maak op een blad uit je tekenschrift een bloemvormig figuur op basis van dezelfde verdeling van een cirkel in zessen als in oefening 7.<p>
	<p>Maak deze constructie zo net mogelijk, laat zien hoe precies je kunt werken en kleur de tekening zo mooi mogelijk in. Je mag naar keuze extra cirkels trekken en/of snijpunten van cirkels met elkaar verbinden. De enige voorwaard is dat de figuur mooi en regelmatig blijft.<br> Mocht je weten wat symmetrisch is, zorg dan dat je figuur perfect symmetrisch is.</p>
	<p>Deze constructie lever je in voor een beoordeling. Zorg dat de naam van de opdracht en je eigen naam bovenaan de bladzijde staat.</p>
