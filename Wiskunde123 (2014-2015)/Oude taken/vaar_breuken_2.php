<h1>Rekenen met breuken 2</h1>

<div class="tekstblok theorie vaardigheden">
	<h3 class="titel vaardigheden">Inleiding</h3>
	<p>
		In de taak <a href="index2.php?p=vaar_breuken_1">Rekenen met breuken 1</a> heb je geleerd hoe je helen uit een breuk haalt, helen in een breuk stopt, hoe je breuken gelijknamig maakt en vereenvoudigt. Dit zijn belangrijke vaardigheden voor de volgende stap in het rekenen met breuken. 
	</p>
	<h3>Doelen</h3>
	<p>
	 Deze taak gaat over het <b>optellen</b> en <b>aftrekken</b> van (gemengde) breuken.
	</p>
	<p>
		<u>Voorbeeld</u>:
		`1 2/3 - 3/4=5/3 - 3/4 = 20/12 - 9/12 = 11/12` 		
	</p>
	<p>
		Je leert (gemengde) breuken op te tellen en van elkaar af te trekken met behulp van gelijknamig maken, helen erin stoppen en het <em>lenen van helen</em> .
	</p>
	<h3>Werkwijze</h3>
	<p>
		 In deze taak maken we veel gebruik van de website <a href="http://www.sommenfabriek.nl" target="_blank">www.sommenfabriek.nl</a> en de <a href="http://ws.fisme.science.uu.nl/dwo/frameset.html" target="_blank">DWO</a>. Daar ga je instructieclips bekijken, uitleg lezen en/of oefenen. Je kunt daar ook extra uitleg en oefening vinden. Lees de theoriestukken, bekijk de instructieclips en maak de opdrachten om je de stof eigen te maken. Succes.
	</p>
</div>

