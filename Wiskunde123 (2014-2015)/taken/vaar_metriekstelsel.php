	<h3>Theorie D Inhoudsmaten</h3>
	<p><img src="afbeeldingen/vaardigheden_metriekstelsel/melkpak.png" width="50px" class="rightfloat">De inhoud van bijvoorbeeld een pak melk uit de supermarkt bereken je door lengte &times; breedte &times; hoogte te doen. Een pak melk is driedimensionaal (3D). De maten van een 1 liter pak zijn: 7&times;7&times;24,5 cm, dus de inhoud is 7&sdot;7&sdot;24,5 = 1200,5 cm<sup>3</sup>. Maar in het "dakje" van het melkpak zit geen melk, er zit 1000 cm<sup>3</sup> melk in een literpak. Uit dit voorbeeld leren we dat 1000 cm<sup>3</sup> hetzelfde is als 1 liter.</p>
	<p>We kunnen 1000 cm<sup>3</sup> omrekenen naar &hellip; dm<sup>3</sup>, namelijk: 1000 cm<sup>3</sup> = 10&sdot;10&sdot;10 cm = 1&sdot;1&sdot;1 dm = 1 dm<sup>3</sup>. En de afspraak is dat 1 dm<sup>3</sup> = 1 liter.	<img src="afbeeldingen/vaardigheden_metriekstelsel/inhoud_ladder.png" width="300px" class="rightfloat"></p>
	<p><img src="afbeeldingen/vaardigheden_metriekstelsel/liter_ladder.png" width="200px" class="centerfloat"><br>
	<b>Let op</b>: Zoals je in het plaatje hierboven kunt zien, zitten er 1000 milliliters in één liter en is 1 ml gelijk aan 1 cm<sup>3</sup>. Dus de liters gaan in stapjes van 10, terwijl de kubieke meters in stapjes van 1000 gaan.</p>
	<p><b>Ezelsbruggetje</b> daarbij is dat als je inhoud uitdrukt in liters (dus liters <b><font color="blue">tot de macht één</font></b>) komt er steeds 1 nul bij of af. Maar als je inhoud uitdrukt in m<sup>3</sup> komen er steeds <font color="blue"><b>3</b></font> nullen bij of gaan eraf. Als je het over inhoud hebt, heb je het ook altijd over een <font color="blue"><b>drie</b></font>dimensionaal figuur.</p>
	<p><b>Voorbeeld</b>:<br>
		<div class="tekstblok opdracht">
			<ol type="a">Reken uit wat er op de puntjes moet staan.
				<li>2,5 m<sup>3</sup> = ... cm<sup>3</sup></li>
