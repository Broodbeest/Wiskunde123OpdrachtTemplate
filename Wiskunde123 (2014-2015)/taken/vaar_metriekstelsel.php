		Op 23 september 1999 gaat de Mars Climate Orbiter verloren. De ruimtesonde nadert Mars te dicht en verbrandt in de atmosfeer. Het verlies bleek veroorzaakt door een menselijk fout: de "stuurman" rekende in inches, terwijl de software van de sonde was geprogrammeerd in centimeters, een kostbare vergissing (327,6 miljoen dollar)!
	</p>
	<p>
		Een <b> <a href="http://nl.wikipedia.org/wiki/Metriek_stelsel" target="_blank">metriek stelsel</a></b> is een systeem van <b>uniforme standaardeenheden</b> voor het meten van bijvoorbeeld afstand, gewicht en temperatuur. De invoering van het metriek decimaal stelsel gebeurde tijdens de Franse Revolutie.
	</p>
	<p>
		Tot eind 18e eeuw bezat elke stad een eigen systeem van standaardmaten. Dat was lastig voor koopmannen die tussen steden reisden of voor wetenschappers. Al in de 16e en 17e eeuw probeerden wetenschappers algemene maten vast te leggen die iedereen zou aanhouden.
	</p>
	<p>
		<img src="afbeeldingen/vaardigheden_metriekstelsel/laptop_inch.png" width="200px" class="rightfloat"> 
		In het verhaal over de ruimtesonde las je al over de inch en de centimeter. Officieel is de inch geen Nederlandse eenheid, toch wordt die nog vaak gebruikt: bijvoorbeeld de lengte van de diagonaal van jouw laptopscherm wordt in inch uitgedrukt. Zo is de diagonaal van het scherm in het plaatje hiernaast 15,6 inch, dus 39,62 centimeter lang.
	</p>
