	<h3>Theorie C Oppervlakte maten</h3>
	<p><img src="afbeeldingen/vaardigheden_metriekstelsel/opp_ladder.png" width="300px" class="rightfloat">De oppervlakte van een rechthoek bereken je door lengte &times; breedte te doen. Bijvoorbeeld 3 cm &times; 5 cm = 15 cm<sup>2</sup>. Zoals je ziet, vermenigvuldig je centimeters met centimeters en iets met zichzelf vermenigvuldigen is een kwadraat. cm<sup>2</sup> spreek je uit als <b>vierkante</b> centimeter en m<sup>2</sup> als <b>vierkante</b> meter.</p>
	<p>Eén vierkante centimeter is een vierkant dat 1 cm lang en 1 cm breed is. Twee vierkante centimeters is twee vierkantjes van 1 bij 1 cm, of een rechthoek van 1 cm breed en 2 cm lang (zie de figuur hieronder).</p>
	<img src="afbeeldingen/vaardigheden_metriekstelsel/vierkante_centimeter.png" width="180px"><img src="afbeeldingen/vaardigheden_metriekstelsel/opp_omrekenen.png" width="250 px" class="rightfloat"><p>In één centimeter passen 10 millimeters, dus <nobr>1 cm<sup>2</sup></nobr> = <nobr>10 mm &times; 10 mm</nobr> = <nobr>100 mm<sup>2</sup></nobr> (zie de afbeelding hiernaast).</p>
	<p><b>Let op</b>: Bij oppervlaktes is één stap hoger of lager op de trap dus &times; of &divide; <font color="blue"><b>100</b></font>.</p>
	<p>
		<b>Ezelsbruggetje</b> daarbij is dat oppervlakte altijd uitgedrukt wordt in m<sup>2</sup>, een m van meter met daarboven een <font color="blue"><b>2</b></font> van <font color="blue"><b>twee</b></font> nullen.
	</p>
