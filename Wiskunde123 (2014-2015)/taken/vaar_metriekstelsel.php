		</div>
	</p>
	<p><b>Uitwerking</b><br>
		Van kubieke meter naar kubieke centimeter is twee traptreden, dus moet 2,5 kubieke meter twee keer met <nobr>1 000</nobr> worden vermenigvuldigd: <nobr>2,5&sdot;1000&sdot;1000 = 2 500 000 cm<sup>3</sup></nobr>.
