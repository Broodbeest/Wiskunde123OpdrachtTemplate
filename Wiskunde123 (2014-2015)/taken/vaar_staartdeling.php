	<h3>Theorie A De hapmethode</h3>
	<p>Op de basisschool heb je geleerd hoe je delingen met grotere of moeilijkere getallen kunt uitvoeren. Sommigen gebruiken dan een <b>aanpak</b> waarbij je steeds 'happen' uit het deeltal neemt. Die 'happen' zijn dan <b>veelvouden</b> van de deler.<br>
