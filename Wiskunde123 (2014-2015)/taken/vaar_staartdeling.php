<!-- theorie B -->
<div class="tekstblok theorie vaardigheden">
	<h3>Theorie B De staartdeling</h3>
	<p>De hapmethode is een prima methode, vooral als je veel tijd hebt. Er is een snellere methode en die wordt de <b>staartdeling</b> genoemd. Het voordeel van de staartdeling is dat die altijd werkt én dat je makkelijk door kunt delen achter de komma.</p>
	
	<p>Bekijk de instructieclip 'staardelingen zonder rest' hieronder.<br>Merk op dat de deler voor de streepjes staat en het deeltal er tussen. Dit is voor veel mensen verwarrend, omdat het in de opgave andersom staat.</p>
	<iframe width="640" height="360" src="http://www.youtube.com/embed/4PyzMWQ-7B8?feature=player_detailpage" frameborder="0" allowfullscreen></iframe>
	
	<p>Nog een voorbeeld:</p>
	<p>258 : 6 =</p>
	<p>Uitwerking:</p>
	<img src="afbeeldingen/staartdeling/theorie_staartdeling_uitwerking.png" alt="theorie_staartdeling_uitwerking" width="" height="" />
	<p>Eigenlijk trek je, net als bij de hapmethode, steeds een veelvoud van de deler van het deeltal af. Met als verschil dat je nu steeds in één keer het grootst mogelijke deel ervan af haalt.</p>
	<p>Kijk maar. Bij 'Stap 1' zie je dat je er nog niks van af kunt halen. Maar dan bij 'Stap 2' trek je in één keer 240 af. 240 is een veelvoud van 6, namelijk 40 × 6. Bij 'Stap 3' kun je nog 18 aftrekken en dat is ook een veelvoud van 6, namelijk 3 × 6.</p>
	<p>In totaal heb je dus 43 × 6 van het deeltal afgetrokken en houd je geen rest over.</p> 
	
	<p>Mocht je al een keer willen zien hoe Mark het aanpakt, ga dan naar <a href="http://www.goedrekenonderwijs.nl/delen-met-hele-getallen" target="_blank">Mark de Rekenmeester</a>. Zijn clips zijn ook wat korter.</p>
</div>

