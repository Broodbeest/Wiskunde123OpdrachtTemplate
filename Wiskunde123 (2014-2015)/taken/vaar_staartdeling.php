<div class="tekstblok theorie vaardigheden">
	<h3>Theorie C De staartdeling met rest</h3>
	<p>Niet alle delingen gaan mooi op. Soms houd je een rest over. Met behulp van een staartdeling kun je ook met kommagetallen rekenen en eenvoudig doorrekenen achter de komma. Hieronder zie je een voorbeeld van het doordelen achter de komma. In de instructieclips daaronder leer je ook delen met kommagetallen.</p>
	<p><u>Voorbeeld 'staartdeling'</u>:</p>
	<p>123 : 4 =</p>
	<p>Uitwerking:<br>Zet de deler (4) voor de streepjes en het deeltal (123) ertussen.</p>
	<img src="afbeeldingen/staartdeling/theorie_staartdeling_stappen.png" alt="Schermafbeelding%202013-04-23%20om%2013.39.31" width="" height="" />
	
	<p>Hieronder nog een aantal instructieclips van Mark de Rekenmeester over staartdelingen:</p>
	<ul>
		<li>als je na de komma moet doorreken '7 : 2';</li>
		<li>als je meteen al achter de komma moet reken '1 : 8';</li> 
		<li>als het deeltal een kommagetal is '28,8 : 24' en;</li>
		<li>als de deler een kommagetal is '36 : 1,2'.</li>
