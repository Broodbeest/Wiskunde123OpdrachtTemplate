		<p>
			Je ziet hier een frequentietabel van de rapportcijfers voor het vak "science" in klas B2f. Zo'n frequentietabel is redelijk overzichtelijk, maar toch wil je (bijvoorbeeld als je klassen met elkaar vergelijkt) niet altijd met alle gegevens voor de dag komen. Je wilt de gegevens samenvatten.
Er zijn drie getallen die deze frequentieverdeling samenvatten:
		</p>
		<ul>
			<li>
				de modus (het modale cijfer), de waarneming die het vaakst voorkomt dus de grootste frequentie heeft;
			</li>
			<li>
				de mediaan, het middelste cijfer als je ze op volgorde van klein naar groot zet (bij een even aantal het gemiddelde van de middelste twee);
			</li>
			<li>
				het gemiddelde van de waarnemingen.
			</li>
		</ul>
		<p>
			Bij deze frequentieverdeling is 7 het modale cijfer.<br>
		Omdat het aantal cijfers 29 (oneven) is, is de mediaan het 15e cijfer, dus 7.<br>
		Het gemiddelde berekenen bij een frequentietabel kun je al.<br>
		Ga na dat het gemiddelde cijfer ongeveer 6,6 is.<br>
		Zowel de modus, de mediaan als het gemiddelde zijn een soort centrum van een frequentieverdeling waar de getallen omheen gegroepeerd zijn. Het zijn centrummaten. De mediaan en het gemiddelde kun je alleen gebruiken als de waarnemingen uit getallen bestaan.
			</p>
