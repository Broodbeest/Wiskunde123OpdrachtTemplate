	<h3>Theorie A Geld</h3>
	<p>Sinds 1 januari 2002 hebben we in veel Europeese landen de euro als betaalmiddel. Er zijn dus ook landen waar ze een ander betaalmiddel hebben. Het omrekenen van munteenheden komt terug in de taak <a href="index2.php?p=vaar_verhoudingen" target="blank">Verhoudingen</a>.</p>
	<p>Er zijn drie dingen die je moet weten over rekenen met euro's:
		<ol type="1">
			<li>
				Een <u>geldbedrag</u> rond je altijd af op <u>twee decimalen</u>.<br>
				Dus niet &euro;3,7498, maar &euro;3,75.
			</li>
			<li>
				De <u>brandstofprijs per liter</u> wordt op <u>drie decimalen afgerond</u>.<br>
				Een liter benzine kost bijvoorbeeld &euro;1,639. Als je 8 liter tankt, betaal je &euro;13,11 (let op, hier dus weer 2 decimalen).
			</li>
			<li>
				Hele <u>grote bedragen</u> worden soms in <b>tonnen</b> uitgedrukt.<br>
				1 ton is &euro;100 000,-.
			</li>
		</ol>
	</p>
