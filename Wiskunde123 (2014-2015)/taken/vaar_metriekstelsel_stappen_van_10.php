	<h3>Theorie D De trap als ezelsbrug</h3>
	<p>
		<img src="afbeeldingen/vaardigheden_metriekstelsel/lengtegewicht_ladder.png" width="300px" class="rightfloat">
		Een andere manier om te onthouden wat je moet doen om van meters naar centimeters te rekenen is het plaatje hiernaast te gebruiken.
	</p>
	<p>
		<b>Let op</b>: millimeter is een kleinere eenheid dan centimeter, dus heb je meer millimeters nodig om een centimeter te maken. Daarom is van centimeter naar millimeter &times;10 en van millimeter naar centimeter &divide;10.
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
		<ol type="a">
			Neem over en vul in:
			<li>2,5 m = ... cm</li>
		</ol>
	</p>
	<p><u>Uitwerking</u><br>
		Van meter naar centimeter is twee traptreden, dus moet je <nobr>2,5 &times;10 &times;10</nobr> doen.<br>
		2,5 m = 250 cm.
	</p>
