	<p>
		<b>Let op</b>: met een komma geef je dus een uur en een zoveelste deel van een uur aan en met een dubbele punt geef je een uur en minuten aan.
	</p>
	<p>
		<u>1</u>:03:30 <u>uur</u> betekent <u>1 uur</u>, 3 minuten en 30 seconden.<br>
		<u>3</u>:30 <u>minuten</u> betekent <u>3 minuten</u> en 30 seconden.<br>
		De eenheid, uur, minuten of seconden, zegt iets over het eerste getal.
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
		Hoeveel uren en minuten is 156 minuten?
	</p>
	<p>
		<u>Uitwerking</u>:<br>
		In √©√©n uur zitten 60 minuten. Er passen 2 uur = 120 minuten in 156 minuten. Er blijven dan 156 - 120 = 36 minuten over.<br>
		156 minuten is 2:36 uur.
	</p>
