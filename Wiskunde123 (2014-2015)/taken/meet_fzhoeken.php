<!-- theorie A -->
<div class="tekstblok theorie meetkunde">
	<h3>Theorie A F-hoeken</h3>
	<p>
		<div class="tekstballon_theorie">
			<p>
				Een hoek is het verschil in richtingen van twee lijnen
			</p>
		</div>
	</p>
	<p>
		In de afbleeding uit de opdracht hiervoor zijn &ang;<em>P</em><sub>4</sub> en &ang;<em>Q</em><sub>4</sub> even groot. De lijnen die aan die hoeken grenzen vormen samen - met een beetje fantasie - de hoofdletter F.
	</p>
	<p>
		<img src="afbeeldingen/meet_fzhoeken/fhoek_th.png" width="100px" class="leftfloat">
		Als twee <b>evenwijdige</b> lijnen worden gesneden door een derde lijn, zijn er altijd hoeken te vinden die even groot zijn. Want het verschil in richting tussen twee lijnen in het ene snijpunt is precies het verschil in richting in het andere snijpunt.<br>
		In het plaatje hierboven zijn de hoeken die even groot zijn gemarkeerd met het symbool &clubs;.
	</p>
	<p>
		De hoeken &ang;<em>P</em> en &ang;<em>Q</em> zijn even groot omdat ze samen een <b>F-hoek</b> vormen.
	</p>
</div>

