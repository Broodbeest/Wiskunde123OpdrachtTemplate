<h1>F- en Z-hoeken</h1>

<!-- Inleiding -->
<div class="tekstblok theorie meetkunde">
	<h3>Inleiding</h3>
	<p>
		<img src="afbeeldingen/meet_fzhoeken/hoeken_th.png" width="200px" class="rightfloat">
		In de taak <a href="index2.php?p=meetkunde_origami" target="blank">Origami</a> heb je geleerd over <b>hoeken</b> en hoe je hoeken kunt meten. Je leerde dat een hoek de ruimte is tussen twee lijnen. Maar je kunt een hoek ook opvatten als het <b>verschil in richting</b>. In het plaatje hiernaast gaat lijn <em>l</em> naar het noorden en lijn <em>m</em> naar het noord-oosten. Het verschil in richting is in dit geval 45&deg;.<br>
		In het plaatje hierboven zie je dat lijn <em>m</em> en lijn <em>n</em> <b>evenwijdig</b> lopen, omdat ze allebei één pijltje hebben. Deze twee lijnen zullen elkaar nooit snijden, omdat ze precies dezelfde richting hebben.<br>
		De hoeken &ang;<em>P</em><sub>2</sub> en &ang;<em>P</em><sub>3</sub> heten samen &ang;<em>P</em><sub>23</sub> (spreek uit: hoek pé-twee-drie). De hoeken &ang;<em>P</em><sub>23</sub> en &ang;<em>Q</em><sub>34</sub> (spreek uit: hoek ku-drie-vier) zijn <b>gestrekte hoeken</b>.
	</p>
	<p>
		Tot zo ver de herhaling van wet je al geleerd hebt dit thema.<br>
		Wat je gaat leren is wat <b>F</b>- en <b>Z-hoeken</b> zijn, hoe je ze kunt herkennen en wanneer je ze kan toepassen. Daarnaast ga je leren wat <b>overstaande hoeken</b> zijn en ze leren herkennen en toepassen.
	</p>
</div>
	
	
	
