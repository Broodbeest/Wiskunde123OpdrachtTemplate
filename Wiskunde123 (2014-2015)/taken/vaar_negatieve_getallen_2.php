	<h3>Opdracht 10 Augustus kwam een jaar tekort (havo/vwo)</h3>
	<img src="afbeeldingen/rekenen_met_negatieve_getallen_1/augustus.jpg" alt="augustus" class="rightfloat" width="140"  />
	<p>
		Eén van de beroemdste Romeinse keizers was Augustus.<br>
		Deze keizer leefde van 63 voor Christus (–63 dus) tot 14 na Christus en heette eigenlijk Gaius Octavianus.<br>
		De eretitel "Augustus" (verhevene) werd hem in het jaar 27 v.Chr. door de Romeinse senaat verleend.
	</p>
	<ol>
		<li>Wat heeft deze opgave met negatieve getallen te maken?</li>
		<li>Hoe oud was Augustus toen hem die eretitel werd verleend?</li>
		<li>Augustus was een geadopteerde zoon van Julius Caesar en werd toen hij 19 jaar oud was als diens opvolger aangewezen. In welk jaar was dat?</li>
		<li>In het jaar 40 v.Chr. werd het Romeinse Rijk na de dood van Caesar verdeeld. Augustus (toen nog Octavianus) kreeg het westelijk deel. 9 jaar later herenigde Augustus het Romeinse Rijk door Antonius, die het oostelijke deel beheerde, bij Actium te verslaan. In welk jaar gebeurde dat?</li>
		<li>Hoe oud was Augustus toen hij stierf?</li>
		<li>Hoeveel jaar was hij toen heerser over het gehele Romeinse Rijk geweest?</li>
	</ol>
	<div class="hider">
		<div class="showhide">&rarr;</div>
		<div class="antwoord">
			<h3>Uitwerkingen</h3>
			<ol>
				<li>De jaartallen voor Christus zijn negatieve getallen.</li>
				<li>Hij is geboren in 63 v.Chr. en in 27 v.Chr. krijgt hij zijn eretitel.<br>63 - 27 = 36<br>Hij was 36 jaar oud.<br>Of -27 - -63 = 63. Want om het verschil te berekenen haal je het kleinste getal van het grootste getal af.</li>
				<li>-63 + 19 = -44<br>Hij werd in 44 v.Chr. als opvolger aangewezen.</li>
				<li>-40 + 9 = -31<br>In 31 v.Chr. herenigde Augustus het Romeinse Rijk</li>
				<li>14 + 63 = 77<br>Augustus was 77 jaar oud, toen hij stierf.</li>
				<li>Van 40 v.Chr. tot aan zijn dood in 14 n.Chr.<br>40 + 14 = 54<br>Augustus is 54 jaar keizer geweest.</li>
			</ol>
			<p>
			Merk op dat er eigenlijk geen jaar 0 bestaat, dus alle antwoorden zitter er een jaar naast.
			</p>
