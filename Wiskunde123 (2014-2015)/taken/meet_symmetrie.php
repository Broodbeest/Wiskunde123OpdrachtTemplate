	<h3>Theorie B Draaisymmetrie</h3>
	<p>
		Wat je in de opdracht hiervoor hebt benoemd, is dat het figuur dat je daar draaide, <b>draaisymmetrisch</b> is. De <b>kleinste draaihoek</b> is het aantal graden waarna het figuur er weer hetzelfde uit ziet (het antwoord op vraag <b>b</b>).
	</p>
