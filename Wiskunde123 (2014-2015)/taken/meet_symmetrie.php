<div class="tekstblok theorie meetkunde">
	<h3>Theorie A Vouw- of spiegelsymmetrie</h3>
	<p>
		Een aantal van de vouwlijnen van jouw sneeuwvlok is zijn een <b>symmetrie-as</b>. Een symmetrie-as is de lijn zó dat links en rechts van die lijn elkaars spiebelbeeld zijn. Dit soort symmetrie heet daarom <b>spiegelsymmetrie</b>, maar wordt ook soms <b>lijnsymmetrie</b> genoemd.
	</p>
	<p>
		<img src="afbeeldingen/meetkunde_symmetrie/symmetrische_figuren.png" alt="symmetrische_figuren" width="620" height="200" />
		Spiegelsymmetrie wordt ook wel <b>vouwsymmetrie</b> genoemd, omdat als je langs de symmetrie-as vouwt links precies op rechts valt. In de afbeelding hierboven zijn de symmetrie-assen in rood aangegeven.
	</p>
</div>

