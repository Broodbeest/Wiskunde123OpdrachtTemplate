	<h3>Theorie A Schaal</h3>
		<img src="../afbeeldingen/meet_schaal_koershoek_2t/amersfoort_kaart_large.png" alt="amersfoort_kaart_large" width="250" class="rightfloat" />
		<p>
			Een plattegrond op ware grootte is onhandig. Daarom wordt op deze kaart Amersfoort verkleind tot een handzaam formaat. Met de <strong>schaal</strong> van de kaart kun je de echte afstanden bepalen.
		</p>
		<p>
			Ook bij schaalmodellen kun je met behulp van de schaal de werkelijke maten bepalen.
		</p>
		<p>
			<strong>Schaal is alleen een verhouding</strong>
		</p>
		<p>
			Sommigen denken dat "schaal" iets te maken heeft met centimeters, maar dat klopt niet. Een schaal geeft de <b>verhouding</b> tussen de tekening en de werkelijkheid.
		</p>
		<ul>
			<li>Bij een schaal van 1:1000 is een centimeter op de kaart 1000 cm in werkelijkheid. </li>
			<li>Maar ook is bij een schaal van 1:1000 een millimeter op een model 1000 mm in werkelijkheid. </li>
			<li>En bij diezelfde schaal is de lengte van een gebouw in werkelijkheid 1000 keer zo groot als het model. 
			</li>
		</ul>
			<p>
				Een schaal van 1:1000 spreek je uit als: "1 staat tot duizend" of <nobr>"1 op duizend".</nobr>
			</p>
</div>


 
