	<h3>Theorie F Een min voor de haakjes</h3>
	<p>
		Soms staat er een min voor de haakjes, zoals in onderstaand voorbeeld.<br>
		Eerst tussen de haakjes rekenen en daarna maakt de min het antwoord negatief.
	</p>
	<aside class="tekstballon_theorie" style="width:350px !important;">
	<p>
		`-(11)=-1 &times; 11=-11`<br>
		negatief &times; positief wordt negatief
	</p>
	</aside>
	<p>
		<u>Voorbeeld</u>:<br>
				-(5 - -6) =<br>
				-(5 + 6) =<br>
				-(11) = -11
	</p>
	<p>
		 In het voorbeeld hieronder is de oplossing tussen de haakje negatief en maakt die extra min het antwoord alsnog positief. Want negatief &times; negatief geeft positief.
	</p>
