<h1>Delen met en zonder rest</h1>
	

<div class="tekstblok theorie vaardigheden">
	<img src="afbeeldingen/vaar_delen_met_en_zonder_rest/wippen.jpg" alt="tegengestelde bewerking" width="200" height="" class="rightfloat"/>
	<h3>Inleiding</h3>
	<p>
		Rekenkundige bewerkingen hebben altijd een <b>tegengestelde</b>. Dat is een bewerking die de ander weer ongedaan maakt.
	</p>
	<p>
		Zo is <b>optellen</b> het tegengestelde van <b>aftrekken</b>. 
	</p>
	<p><u>Voorbeeld</u>:</p>
	<p>
		10 - 2 = 8 want 8 + 2 = 10<br>
		18 + 3 = 21 want 21 - 3 = 18
	</p>
	<p>
		En is <b>delen</b> het tegengestelde van <b>vermenigvuldigen</b>.
	</p>
	<p>
		<u>Voorbeeld</u>:
	</p>
	<p>
		10 × 2 = 20 want 20 : 2 = 10<br>
		18 : 3 = 6 want 6 × 3 = 18
	</p>
	<p>
		Merk op dat kennis van de tafels hier weer goed van pas komt!
	</p>
	
	<h3>Doelen:</h3>
	<ul>
		<li>
			leren begrip <em>tegengestelde bewerkingen</em>;
		</li>
		<li>
			leren begrippen <em>deelbaar</em>, <em>rest</em>, <em>deeltal</em> en <em>deler</em>;
		</li>
		<li>
			leren delen met behulp van vermenigvuldigen.
		</li>
	</ul>
	
