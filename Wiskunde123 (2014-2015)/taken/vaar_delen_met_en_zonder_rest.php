<div class="tekstblok theorie vaardigheden">
	<h3>Theorie D Rest</h3>
	<img src="afbeeldingen/vaar_delen_met_en_zonder_rest/theorie_d_snoep.jpg" alt="theorie_d_snoep" width="200" height="" class="rightfloat" />
	<p>Soms komt delen niet mooi uit, dan houd je een <b>rest</b> over. Zoals in het geval van de kinderen en de snoepjes. Je kon elk kind 4 snoepjes geven, daarna hield je 3 snoepjes over. In rekentaal is dit:</p>
	<p>23 : 5 = 4 rest 3, want 4 × 5 = 20 en je houdt er 3 over.</p>
	<p><u>Voorbeeld</u>:</p>
	<p>Bereken en geef het antwoord aan met rest.<br>
	7 : 3 = ... </p>
	<p>Uitwerking<br>
	7 : 3 = 2 rest 1, want 2 × 3 = 6 en je houdt 1 over.</p>
	<p>Tip: bedenk weer of het deeltal in de tafel van de deler zit. Als dat niet zo is, zoek je naar het grootste getal dat kleiner is dan het deeltal dat wél in de tafel van de deler voorkomt.</p>
	<p>In het voorbeeld zie je dat 7 niet in de tafel van 3 voorkomt. 6 is kleiner dan 7 en komt wel in de tafel van 3 voor. Je houdt dus rest 1 over.</p>
</div>


