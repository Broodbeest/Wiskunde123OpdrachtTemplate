	<h3>Theorie C Zonder rest</h3>
	<p>Een getal is <b>deelbaar</b> door een ander getal als er een mooi heel getal uitkomt.</p>
	<img src="afbeeldingen/vaar_delen_met_en_zonder_rest/delen_theorie_c.png" alt="delen_theorie_c" class="rightfloat" width="150" height="" />
	<p>De delingen van opdracht 1 en 2 kwamen mooi uit. Je houdt niks over, er is geen <b>rest</b>. Dat komt omdat het deeltal in de tafel van de deler zit.</p>
	<p>Je kon je antwoorden controleren met behulp van een vermenigvuldiging.<br>
	Je maakt de opgave als het ware in tegengestelde richting.</p>
	<p>Vermenigvuldigen en delen zijn elkaars <b>tegengestelde bewerking</b>.</p>		
</div>


