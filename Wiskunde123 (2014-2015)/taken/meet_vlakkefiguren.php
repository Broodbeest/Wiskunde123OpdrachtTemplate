<div class="tekstblok opdracht">
	<h3>Opdracht 5 Rechthoek en vierkant</h3>
	<p>
		<iframe scrolling="no" src="https://tube.geogebra.org/material/iframe/id/798563/width/390/height/300/border/888888/rc/false/ai/false/sdz/true/smb/false/stb/false/stbh/true/ld/false/sri/true/at/auto" width="300px" height="231px" style="border:0px;" class="rightfloat"> </iframe>
		Net als een vierkant heeft een rechthoek heeft <u>ook</u> vier hoekpunten en de zijden staan loodrecht op elkaar. Een rechthoek lijkt dus op een vierkant, behalve dat niet álle zijden even lang zijn.
	</p>
	<p>
		In de afbeelding hiernaast zie je een rechthoek. Pak de schuifknop vast en kijk goed wat er gebeurd als je de schuifknop verplaatst.
		<ol>
			<li>
				Kun jij van de rechthoek een vierkant maken?
			</li>
			<li>
				Welke eigenschappen heeft de rechthoek nu <u>extra</u> gekregen?
			</li>
			<li>
				Zou het ook andersom kunnen? Kun je een zijde van een vierkant langer of korter maken waardoor het een rechthoek wordt?
			</li>
		</ol>
	</p>
	<div class="hider">
		<div class="showhide">&rarr;</div>
		<div class="antwoord">
			<ol>
				<li>De figuur <em>ABCD</em> wordt blauw als het een vierkant is.</li>
				<li>Bij een rechthoek zijn overstaande zijden even lang (<em>AB</em> = <em>CD</em> en <em>BC</em> = <em>AD</em>), maar niet <u>allemaal</u> even lang (<em>AB</em> ≠ <em>BC</em>).<br>
					De diagonalen van een rechthoek staan niet loodrecht op elkaar (tenzij alle zijden even lang zijn).</li>
				<li>Nee, van alle vierkanten zijn alle zijden even lang én staan de diagonalen loodrecht op elkaar.</li>
			</ol>
		</div>
	</div>
