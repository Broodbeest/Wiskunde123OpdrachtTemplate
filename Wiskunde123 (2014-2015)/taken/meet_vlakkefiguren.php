<div class="tekstblok theorie meetkunde">
	<h3>Theorie A Vierkant en rechthoek</h3>
	<p>
		Een <b>diagonaal</b> verbindt twee hoekpunten door middel van een lijnstuk welke <u>binnen</u> het figuur ligt.
	</p>
	<p>
		In de opdrachten hiervoor heb je gezien dat een <b>vierkant</b> en een <b>rechthoek</b> heel verschillend zijn, maar ook op elkaar lijken.
		<ol type="1">
			Een vierkant...
			<li>
				heeft vier zijden die allemaal even lang zijn;
			</li>
			<li>
				heeft twee paar tegenoverstaande zijden die evenwijdig zijn;
			</li>
			<li>
				heeft vier hoeken die recht zijn;
			</li>
			<li>
				en heeft twee diagonalen die loodrecht op elkaar staan.
			</li>
		</ol>
	</p>
	<p>
		<ol type="1">
			Van een rechthoek...
			<li>
				zijn tegenoverstaande zijden even lang;
			</li>
			<li>
				zijn tegenoverstaande zijden evenwijdig;
			</li>
			<li>
				zijn de hoeken recht;
			</li>
			<li>
				en zijn de diagonalen even lang en delen elkaar in vier even lange lijnstukken.
			</li>
		</ol>
	</p>
</div>

