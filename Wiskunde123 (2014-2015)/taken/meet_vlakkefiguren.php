<div class="tekstblok theorie meetkunde">
	<h3>Vlakke figuren</h3>
	<p>
		Naast driehoeken zijn er een heleboel andere meetkundige figuren, zoals vierkanten, rechthoeken, ruiten, vliegers, trapezia, parallelogrammen, regelmatige vijfhoeken... Te veel om op te noemen.<br>
		Je begint met <b>vierhoeken</b>, dat zijn vlakke figuren met vier hoekpunten en vier zijden. De bekendste vierhoeken zijn het vierkant en de rechthoek.
	</p>
	<p>
		Met behulp van symmetrieën en vendiagrammen ga je veel vlakke figuren leren kennen.
	</p>
</div>

