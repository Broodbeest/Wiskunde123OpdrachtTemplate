		<h3>Theorie E Globale grafieken</h3>
		<p>Bij een tabel kun je een grafiek tekenen. Tot nog toe tekenden we een vloeiende lijn door de punten. Die vloeiende lijn is een schatting van wat er in het echt gebeurd is, je kunt vrij nauwkeurig zeggen hoe warm het op een bepaalde dag geweest moet zijn.
		<p>Langs de assen van <b>globale grafieken</b> staat wel de grootheid, maar geen getallen. Je kunt dus wel zien of iets groter wordt, maar niet hoeveel groter. Je krijgt dus alleen maar een globaal beeld van de situatie, maar je kunt er niet mee rekenen.</p>
	</div>

