<!--Theorie Bordjesmethode-->
<div class="tekstblok theorie analyse">
	<h3>Theorie D Bordjesmethode</h3>
	<p>
		In de opgave hierboven heb je de grafiek gebruikt om uit te zoeken hoeveel rondjes Youssef moet lopen om €10 te verdienen.
		De woordformule die we gebruikten was:<br>
		<em>Bedrag</em> `= 1`<em>x aantal rondjes</em> `+ 3`. <br>
		Deze formule mag je ook omdraaien!<br>
		`1`<em>x aantal rondjes</em> `+ 3 =`<em>Bedrag</em>
		<br>
		We kunnen van deze woordformule ook de volgende letterformule maken:<br>
		`1x + 3 = y` <br>
		Hierin is `x` het aantal rondjes en `y` het bedrag. Als je nu op de plek van de `y` het bedrag `10` invult, dan krijg je:<br>
		`1x + 3 =10` <br>
		Je mag in deze vergelijking de `1` voor de `x` weglaten!
	</p> 
	<p>
		`x + 3 = 10`	is een voorbeeld van een <b>vergelijking</b>. In een vergelijking staat altijd een onbekend getal. Het onbekende getal geven we aan met een letter. Hier is dat de letter `x`. Dit noemen we de <b>variabele</b>.
	</p>
	<p>
		We zoeken de waarde van `x` waarvoor de som klopt. Het vinden van de oplosing heet binnen de wiskunde het <b>oplossen van een vergelijking</b>.
	</p>
	<p>	
		De waarde van `x` waarvoor de vergelijking klopt, heet een <b>oplossing</b> van de vergelijking.</b> 
	</p>
	<p>
		Vergelijkingen kan je oplossen met behulp van de <b>bordjesmethode</b>.<br>
		De bordjesmethode werkt zo:
	</p>
	<table style="vertical-align=top" class="stappenplan">
		<tr>
			<td>Stap 1</td>
			<td>
				Neem de vergelijking over.
			</td>
			<td>
				`x + 3 = 10`
			</td>
		</tr>	
		<tr>
			<td>Stap 2</td>
			<td>
				Leg een bordje op `x`.
			</td>
			<td>
					<span class="formula"> ? </span> `+`  `3 = 10`
			</td>
		</tr>			
 		<tr>		
 			<td>Stap 3</td>
			<td>
				Onder het bordje moet `7` liggen, want:
			</td>
			<td>
				`7 + 3 = 10`<br>
			</td>
		</tr>	
		<tr>		
 			<td>Stap 4</td>
			<td>
				Nu weet je dat `x` gelijk is aan `7`.
			</td>
			<td>
				`x = 7`
			</td>
		</tr>	
		<tr>
			<td>Stap 7</td>
			<td>
				Controleer je antwoord door dit in te vullen.
			</td>
			<td>
				`7+3=10` Klopt!
			</td>
		</tr>
	</table>
