<div class="tekstblok theorie vaardigheden">
	<h3>Theorie 3</h3>
	<p>
	Het getal 9 staat boven het getal 8 op de getallenlijn. Je zegt dan dat 9 groter is dan 8. In wiskundetaal schrijf je dat als volgt op: 9 &#62; 8. Andersom is 8 kleiner dan 9, in wiskundetaal schrijf je: 8 &#60; 9.
	</p>
	<p>
	De getallen onder de nul: … , -5, -4, -3, -2 en -1 noem je <b>negatieve</b> (gehele) getallen. De getallen boven de nul: 1, 2, 3, 4, … noem je <b>positieve</b> (gehele) getallen. Negatieve getallen zijn altijd kleiner dan positieve getallen. Maar hoe zit dat met twee negatieve getallen? Bekijk de voorbeelden hieronder.
	</p>
	<p>
		<u>Voorbeeld</u>:
	</p>	
	<table>
		<tr>
			<td>2 &#60; 7</td>
			<td>betekent</td>
			<td>"2 is kleiner dan 7"</td>
		</tr>
		<tr>
			<td>-3 &#60; 2</td>
			<td>betekent</td>
			<td>"-3 is kleiner dan 2"</td>
		</tr>
		<tr>
			<td>8 &#62; -13</td>
			<td>betekent</td>
			<td>"8 is groter dan -13"</td>
		</tr>
		<tr>
			<td>-3 &#60; -1</td>
			<td>betekent</td>
			<td>"-3 is kleiner dan -1"</td>
		</tr>
	</table>
</div>


