	<h3>Theorie 2</h3>
	<p>
		Een lijn met getallen erbij heet een <b>getallenlijn</b>. Een thermometer is vergelijkbaar met een verticale getallenlijn. De getallen boven de nul noemen we <b>positief</b> en de getallen onder nul <b>negatief</b>. Het getal 0 is niet positief en ook niet negatief.
	</p>
	<p>
		In de praktijk gebruik je al <b>negatieve getallen</b>. Denk maar aan de temperatuur als het vriest, als je naar de kelder gaat met de lift of als je bijvoorbeeld je saldo checkt.
	</p>
</div>

<div class="tekstblok opdracht">
	<h3>Opdracht 2</h3>
<p>
	In de volgende situatie maak je gebruik van negatieve getallen, neem eerst de sommen over voordat je de opgaven maakt.
</p>
<ol>
	<li>
		Temperatuur<br>
		Vandaag is het 7 graden. Morgen wordt het 10 graden kouder.<br>
		Wat is de temperatuur morgen?
		<br>
		7 - 10 = ....<br>
	</li>

	<li>
		Flatgebouw<br>
		Peter woont op de vijfde verdieping. Hij gaat zijn fiets halen in de kelder. 
		Hoeveel verdiepingen gaat hij dan met de lift naar beneden?
		<br>
		5 - .... =  -1<br>
	</li>

	<li>
		Saldo<br>
		Sonja heeft 40 euro op haar rekening staan. Ze heeft hele mooie schoenen in de winkel gezien.<br>
		Ze kosten 55 euro.<br>
		Hoeveel komt ze rood te staan als ze die schoenen koopt?<br>
		40 - 55 = ...<br>
	</li>
</ol>
<div class="hider">
		<div class="showhide">&rarr;</div>
		<div class="antwoord">
			<h3>Uitwerkingen</h3>
			<ol>
				<li>7 - 10 = -3, dus morgen vriest het 3 graden.</li>
				<li>5 - 6 =  -1, hij gaat zes verdiepingen omlaag.</li>
				<li>40 - 55 = -15, ze zal 15 euro rood komen te staan als ze de schoenen koopt.</li>
			</ol>
		</div>
	</div>
