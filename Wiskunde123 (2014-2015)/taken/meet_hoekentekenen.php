	<h3>Theorie C Hoeken tekenen</h3>
	<p>
		Met je geodriehoek kun je niet alleen hoeken opmeten, je kunt ze ook zelf gaan tekenen. Hier staat expres het werkwoord <b>tekenen</b> en niet <b>construeren</b>. Construeren betekent dat je alleen een passer en een liniaal gebruikt. Tekenen betekent dat je nog steeds een liniaal of geodriehoek gebruikt voor rechte lijnen, maar dat je een geodriehoek mag gebruiken om een bepaalde hoek te maken.
	</p>
	<p>
		<u>Voorbeeld 1</u><br>
		Teken een hoek van 128&deg;.
	</p>
	<p>
		<u>Uitwerking</u>:<br>
