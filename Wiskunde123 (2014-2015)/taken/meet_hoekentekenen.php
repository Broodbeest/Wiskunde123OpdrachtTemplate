		<h3>Theorie D Door elkaar</h3>
		<p>Soms mag je een hoek maken met behulp van je geodriehoek, en soms moet je een passer en een liniaal gebruiken. Let daarbij op <b>signaalwoorden</b>: woorden waardoor er bij jou direct een belletje zou moeten gaan rinkelen.<br>
		Signaalwoorden kunnen bijvoorbeeld zijn: construeer, teken, meet of bereken.</p>
		<p>Als je een opgave gaat maken, kun je onderstaande stappenplan gebruiken:<br>
			<table class="stappenplan">
				<tr>
					<td>Stap 1</td><td>Lees de vraag goed door, probeer signaalwoorden te herkennen.</td>
				</tr>
				<tr>
					<td>Stap 2</td><td>Schrijf op wat je al weet, ook al staat 't in de opgave! Daarmee laat je zien dat je de opgave hebt begrepen.</td>
				</tr>
				<tr>
					<td>Stap 3</td><td>Geef antwoord op de vraag.</td>
				</tr>
				<tr>
					<td>Stap 4</td><td>Lees de opgave nog een keer. Heb je antwoord gegeven op de vraag?</td>
				</tr>
			</table>
		</p>
		<p>Schrijf áltijd op hoe je aan je antwoord komt, want alleen een getal als antwoord is op een toets maximaal 1 punt waard!</p>
	</div>

