		<p>In de taak <a href="index2.php?p=meetkunde_hoeken" target="_blank">Rekenen met hoeken</a> heb je geleerd dat elke hoek uit te drukken is in graden. De bekendste hoek, de rechte hoek, is 90&deg;.<br>
			In de taak <a href="index2.php?p=meetkunde_fzhoeken" target="_blank">F- en Z-hoeken</a> leerden we dat een hoek kleiner dan 90&deg; een scherpe hoek heet en een hoek groter dan 90&deg; een stompe hoek.</p>
