		<h3>Opdracht 10 Geodriehoek</h3>
		<p>In deze opdracht ga je een bijzondere driehoek maken door zowel te tekenen als te construeren.
			<ol>
				<li>Trek een lijn <em>l</em>, zet daarop een willekeurig punt <em>P</em>.</li>
				<li>Trek een lijn <em>m</em> door <em>P</em> waardoor &ang;<em>P</em> = 45&deg;.</li>
				<li>Zet punt <em>R</em> op lijn <em>m</em>, zodat <em>PR</em> de lengte van je pink heeft.</li>
				<li>Richt de loodlijn op <em>m</em> op die door <em>R</em> gaat.</li>
				<li>Noem het snijpunt van de loodlijn met lijn <em>l</em> <em>Q</em>.</li>
				<li>Meet hoe groot &ang;<em>Q</em> is.</li>
				<li>Bereken hoe groot &ang;<em>Q</em> zou moeten zijn.</li>
			</ol>
