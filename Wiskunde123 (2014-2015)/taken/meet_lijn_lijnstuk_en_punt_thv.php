<div class="tekstblok theorie meetkunde">
	<h3>Theorie A Lijnen, lijnstukken en punten</h3>
	<p>
		In de wiskunde zijn afspraken gemaakt over wat een lijn, een punt en een lijnstuk zijn. 
	</p>
	<p>
	<iframe scrolling="no" src="https://www.geogebratube.org/material/iframe/id/129452/width/300/height/300/border/ccffe5/rc/false/ai/false/sdz/true/smb/false/stb/false/stbh/true/ld/false/sri/true/at/preferhtml5" width="300px" height="300px" style="border:0px;" class="rightfloat"> </iframe>
	
		Een <b>lijn</b> geef je aan met een kleine letter, bijvoorbeeld lijn <em>l</em>, <em>m</em> of <em>n</em>. Een lijn heeft geen begin- of eindpunt, dus loopt aan beide kanten oneindig ver door.
	</p>

	<p>
		Een <b>punt</b> geef je aan met een hoofdletter, bijvoorbeeld <em>P</em>, <em>Q</em> of <em>R</em>. Een punt kan op een lijn liggen. In de afbeelding ligt <em>R</em> op <em>n</em>.
	</p>
	<p>
		Een <b>lijnstuk</b> gaat van punt tot punt, bijvoorbeeld lijnstuk <em>PQ</em>. Een lijnstuk heeft een bepaalde lengte. Als de afstand tussen <em>P</em> en <em>Q</em> 4 cm is, dan noteer je dat als: <em>PQ</em> = 4 cm.
	</p>
	<!-- 
