						<em>m</em> &perp; <em>n</em> en <em>AB</em> &perp; <em>CD</em>
					</figcaption>
				</figure>
			</td>
		</tr>
	</table>

	<p>
		Als lijn <em>m</em> loodrecht op lijn <em>n</em> staat, noteer je: <nobr><em>m</em> &perp; <em>n</em>.</nobr> In een constructie of tekening zet je dan een klein haakje bij het snijpunt.
	</p>
	
	<p>
		<img src="afbeeldingen/meetkunde_arabesk/theorie_b_loodlijnlood_met_muur.png" alt="theorie_b_loodlijnlood_met_muur" width="150" height="" class="rightfloat">
		Vroeger gebruikte men een stuk touw met een loden gewicht eraan om muren te stellen. Als het touw recht naar beneden hing, wist je dat de muur recht stond. Daar komt het woord <em>loodrecht</em> vandaan. Zo is een <b>loodlijn</b> een lijn die loodrecht op een andere lijn staat. 
	</p>
	

	<p>
		In de wiskunde kom je loodlijnen nog vaak tegen, bijvoorbeeld <b>hoogtelijnen</b> bij driehoeken.
	</p>
	
	<img src="../afbeeldingen/meet_lijn_lijnstuk_en_punt/theorie_c_raamkozijn.jpg" alt="theorie_c_raamkozijn" width="150" height="" class="rightfloat" />
	<p>
		In de praktijk zie je loodlijnen in raamkozijnen: de verschillende delen van het kozijn zijn meestal loodrecht ten opzichte van elkaar. Maar ook je tafelblad heeft loodrechte hoeken.
	</p>
	<p>
		Kun je zelf ook nog een paar loodrechte lijnen bedenken in de 'gewone wereld'?
	</p>
</div>

