		Theorie C Haakse lijnen staan loodrecht op elkaar
	</h3> 
	<p>
		Lijnen die elkaar precies haaks snijden, staan <b>loodrecht</b> op elkaar. Zo staan de sporten van een ladder loodrecht ten opzichte van de twee zijsteunen.
	</p>
