						<li>Een gelijkzijdige driehoek heeft drie hoeken die precies even groot zijn. <u>Hoekensom</u>: <nobr>&ang;<em>D</em> + &ang;<em>E</em> + &ang;<em>F</em> =</nobr> 180<br>
