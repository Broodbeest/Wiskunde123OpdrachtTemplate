		<img src="afbeeldingen/meetkunde_arabesk/madrasa.jpg" alt="madrasaaf" width="250" height="" class="rightfloat"/>
		<p>De Ali bin Yusuf Madrasa (koranschool) in Marrakesh is een uniek voorbeeld Islamitische architectuur. De naam Bin Yusuf is afgeleid van de Almoravidische sultan Ali Ben Joesoef, die regeerde van 1106 tot 1142.</p>
		<p>Het gebouw is rond 1570 gebouwd en is in 1950 gerestaureerd. De studenten waren gehuisvest in 130 cellen die gelegen zijn rond een binnenplaats die rijkelijk versierd is met cederhout, marmer, zellige-tegelwerk en gipsen ornamenten. De school ontwikkelde zich tot één van de belangrijkste koranscholen in de Islamitische wereld en met 900 studenten tot de grootste in Afrika.</p>
