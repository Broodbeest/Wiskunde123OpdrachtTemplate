		<h3>Theorie B Hellingsgetal en startgetal</h3>
		<p>In opgave 6 heb je misschien ontdekt wat de 18 in de formule voorstelt. Als de schilder 1 uur langer doorwerkt dan moet ik &euro;18 euro meer betalen. Het bedrag wat er bij komt als de schilder 1 uur meer werkt noem je in de wiskunde het <b>hellingsgetal</b>.
		<br>In opgave 6 moest je beschrijven wat de 52 in de formule voorstelt. Als de schilder nog helemaal niets geschilderd heeft dan moet je toch al &euro;52 betalen, dit zijn de vaste kosten en hangen niet af van het aantal uren schilderen. In de wiskunde noemen we dit het <b>begin-</b> of <b>startgetal</b>.</p>
