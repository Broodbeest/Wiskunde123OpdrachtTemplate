	<div class="tekstblok theorie analyse">
		<h3>Theorie A Woordformules</h3>
		<p><img src="afbeeldingen/analyse_2/recept3.png" width="250" class="rightfloat"/></p>
		<p>In alle voorgaande opgaven heb je een bepaalde <b>regelmaat</b> ontdekt.  Het voordeel van deze regelmaat is dat je een berekening kan verzinnen om in een keer voor het 15<sup>e</sup>, of 100<sup>e</sup> figuur het aantal stoelen, munten etc. te berekenen.</p>
		<p>Zo’n recept, of vuistregel, noem je in de wiskunde ook wel een woordformule.</p>
		<p>Kijk nog eens terug naar de opgaven over de tafelschikking (opdracht 1).</p>
		<p><img src="afbeeldingen/analyse_2/theorie_A_tafelschikkingen.png" width="600" /></p>
		<p>De <b><FONT color="red">éne</FONT></b> stoel aan kop van de tafel zit in ieder figuur.</p>
		<p>Als er een tafel bijkomt komt er een stoel onder en boven bij. De regelmaat is dus steeds <b><FONT color="green">2</FONT></b> erbij.</p>
		<p><table>
				<tr>
					<td>Bij figuur <b>1</b> hebben we dus <b><FONT color="red">1</FONT></b> + <b>1</b> &times; <b><FONT color="green">2</FONT></b> stoelen,</td></tr>
				<tr>
					<td>bij figuur <b>2</b> hebben we dus <b><FONT color="red">1</FONT></b> + <b>2</b> &times; <b><FONT color="green">2</FONT><b> stoelen,</td></tr>
				<tr>
					<td>bij figuur <b>3</b> hebben we dus <b><FONT color="red">1</FONT><b> + <b>3</b> &times; <b><FONT color="green">2</FONT></b> stoelen.</td></tr>
			</table>
		</p>	
		<p>Formule:	<em>Aantal stoelen</em> = <b><FONT color="red">1</FONT></b> + <em>nummer figuur</em> &times; <b><FONT color="green">2</FONT></b></p>
		<p>Als we dus het nummer van de figuur weten kunnen we met een formule snel het aantal stoelen van bijvoorbeeld figuur nummer 110 uitrekenen!</p>
	</div>
	
