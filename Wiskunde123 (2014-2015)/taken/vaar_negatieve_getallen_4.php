<div class="tekstblok theorie vaardigheden">
	<h3><a id="theorie_b">Theorie A Een negatief getal delen</a></h3>
	<p>
		Schulden of tekorten kun je aangeven met behulp van negatieve getallen. In opgave hierboven heb je een schuld verdeeld over meerdere personen. Het aantal personen is een positief getal. Je deelt dus een negatief getal door een positief getal.
	</p>
	<p>
		<u>Voorbeeld</u>:
	</p>
	<p>
		Verdeel een schuld van 50 euro over 10 personen:<br>
		-50 : 10 = -5<br>
		Ieder heeft dan 5 euro schuld. 
	</p>
	<p>
		In het voorbeeld deel je een negatief getal door een positief getal. Het antwoord is negatief.
	</p>	
</div>

