<div class="tekstblok theorie vaardigheden">
	<h3>Theorie E Delen door nul is ... </h3>
	<p>
		Vermenigvuldigen en delen zijn elkaars tegengestelde bewerkingen.
	</p>
	<p>	
		<span class="tab_voorbeeld">
			Neem de volgende opgave:
		</span>
		<div class="tab">
			`0 &times; 15 = 0`
		</div>
	</p>
	<p>
		<span class="tab_voorbeeld">
			Tegengesteld is dan:
		</span>
		<div class="tab">
			`0 : 15 = 0`
		</div>
	</p>
	<p>
		Helemaal niks aan de hand. Je hebt 0 vermenigvuldigd of gedeeld. En 0 maal iets is niets en als je niets verdeelt, krijgt ieder niets.
	</p>
	<p>
		Maar wat gebeurt er als je dóór 0 deelt?
	</p>
	<p>	
		<span class="tab_voorbeeld">
			Neem de volgende opgave:
		</span>
		<div class="tab">
			`15 : 0 = ?`
		</div>
	</p>
	<p>
		<span class="tab_voorbeeld">
			Tegengesteld zou zijn:
		</span>
		<div class="tab">
			`? &times; 0 = 15`
		</div>
	</p>
	<p>
		Nu heb je een probleem: je kunt geen enkel getal verzinnen dat je vermenigvuldigt met 0 en dan 15 als antwoord krijgt. Delen door 0 is dan ook onmogelijk, oftewel het kan niet.
	</p>
	<p>
		In de wiskunde zeggen we: <em>delen door nul is flauwekul</em>.
	</p>

		<table class="stappenplan" >
		<tr>
			<td>
				<u>Voorbeeld 1</u>:
			</td>
			<td>
				`(-100*5)/(3-3)=`
			</td>
			<td>
				teller en noemer uitrekenen
			</td>
		</tr>
		<tr>
			<td>

			</td>
			<td>
				`-500/0=` <em>kan niet</em>
			</td>
			<td>
				noemer is 0, delen door nul is flauwekul
			</td>
		</tr>
	</table>
	
	<p>
		Als de noemer gelijk is aan 0, kun je de som niet oplossen.
	</p>
	
	<table class="breuken">
		<tr>
			<td>
				<u>Voorbeeld 2</u>:
