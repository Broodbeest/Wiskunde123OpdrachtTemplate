<div class="tekstblok theorie meetkunde">
	<h3>Theorie A Vierkant en rechthoek</h3>
	<p>
		Een <b>diagonaal</b> verbindt twee hoekpunten met een lijnstuk welke binnen het figuur ligt.
	</p>
	<p>
		In de opdrachten hiervoor heb je gezien dat een <b>vierkant</b> en een <b>rechthoek</b> heel verschillend zijn.
		<ol type="1">
			Een vierkant...
			<li>
				heeft vier zijden die allemaal even lang zijn;
			</li>
			<li>
				heeft vier hoeken die recht zijn;
			</li>
			<li>
				heeft twee diagonalen die loodrecht op elkaar staan;
			</li>
		</ol>
	</p>
	<p>
		<ol type="1">
			Van een rechthoek...
			<li>
				zijn tegenoverstaande zijden even lang;
			</li>
			<li>
				zijn tegenoverstaande zijden evenwijdig;
			</li>
			<li>
				zijn de hoeken recht;
			</li>
			<li>
				zijn de diagonalen even lang en delen elkaar in vier even lange lijnstukken.
			</li>
		</ol>
	</p>
</div>

