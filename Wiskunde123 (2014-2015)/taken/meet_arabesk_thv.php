<div class="tekstblok theorie meetkunde">
	<h3>Inleiding</h3>
	<img src="afbeeldingen/meetkunde_arabesk/moskeeshiraz.png" width="230 px" class="rightfloat">
	<p>
		In de taak <a href="http://wiskunde.vathorstcollege.nl/index2.php?p=meet_bloem_thv">Bloem</a> heb je cirkels leren verdelen zes gelijke stukken. In de taak <a href="http://wiskunde.vathorstcollege.nl/index2.php?p=meet_lijn_lijnstuk_en_punt_thv">Lijn, lijstuk en punt</a> heb je onder andere loodlijnen en vierkanten leren tekenen of construeren. Met deze <b>basisconstructies</b> kun je meetkundige figuren maken.</p>
		<p>
		Een voorbeeld van zo'n figuur zie je op het plaatje: een Islamitische Arabesk. Moskeeën worden versierd met dit soort figuren. In deze taak ga je ook zo'n arabesk maken. Hieraan werk je zowel bij beeldende vorming als bij wiskunde.
	</p>
</div>


<div class="tekstblok opdracht">
	<h3>Opdracht 1 Cirkel</h3>
	<p>
		Als je deze opdracht lastig vindt, kun je kijken bij opdracht 7 en 8 van taak <a href="http://wiskunde.vathorstcollege.nl/index2.php?p=meet_bloem_thv" target="_blank">Bloem</a>.
	</p>
	<ol>
		<li>
			Trek een cirkel met een straal van 3 cm.
		</li>
		<li>
			Maak nu het begin van de bloem. Je hoeft de buitencirkels niet helemaal te trekken, alleen de stukjes bij de eerste cirkel zijn nodig.
		</li>
		<li>
			Zet een punt bij de middelpunten van elke buitencirkel.
		</li>
		<li>
			Verbind drie punten met elkaar zodat je een mooie, regelmatige driehoek krijgt.
		</li>
		<li>
			Meet de zijden van de driehoek op en zet de maten in je tekening.
		</li>
		<li>
			Geef met streepjes aan welke zijden even groot zijn. 
		</li>
		<li>
			Maak tot slot een zeshoek. Geef weer aan met behulp van streepjes welke zijdes gelijk zijn.
	</ol>
</div>


<div class="tekstblok opdracht">
	<h3>Opdracht 2 Teken een vierkant (vmbo-t)</h3>
	<ol>
		<li>
			Teken met behulp van je geodriehoek een vierkant met zijden van 15 cm. Zorg dat de zijden even lang en loodrecht op elkaar staan.
		</li>
		<li>
			Teken de middelloodlijnen van elke zijde.
		</li>
		<li>
			Teken de twee diagonalen.
		</li>
	</ol>
</div>

<div class="tekstblok opdracht">
	<h3>Opdracht 3 Construeer een vierkant (havo/vwo)</h3>
	<ol>
		<li>
			Construeer met behulp van passer en liniaal een vierkant met zijden van 15 cm. Zorg dat de zijden even lang en loodrecht op elkaar staan.
		</li>
		<li>
			Construeer de middelloodlijnen van elke zijde.
		</li>
		<li>
			Teken de diagnonalen.
		</li>
	</ol>
</div>


<div class="tekstblok opdracht">
	<h3>Opdracht 3 Arabesk</a></h3>
	<p>
		Hieronder zie je drie arabesken. Als je die heel vaak zou herhalen kun je een hele muur vullen met deze tekening, want de arabesken passen aan elkaar. Dat heet een vlakvulling.
	</p>
	<p>
		Uit onderstaande arabesken kies jij de moeilijkheidsgraad die bij jou past; hoe meer sterren, hoe moeilijker. Elke Arabesk moet in een vierkant van 15 bij 15 cm passen, zodat jouw Arabesk goed met andere kan worden gecombineerd.
	</p>
	<p>
		Je maakt van die vlakvulling twee keer een arabesk. Eén op papier waarin alle constructielijnen blijven staan en één op dik papier die je artistiek bewerkt<!-- dat zal plaatsvinden tijdens beeldend lessen -->. De geconstrueerde arabesk lever je in voor een beoordeling op gebruik van constructies, de artistieke arabesk zal samen met dezelfde arabesken van leerlingen uit alle brugklassen één grote vlakvulling vormen en ergens in school tentoongesteld worden.
