	<h3>Theorie A Oppervlakte rechthoekige driehoek</h3>
	<p>Om elke rechthoekige driehoek kun je een rechthoek tekenen waarvan jij de oppervlakte uit kunt rekenen. De helft van de oppervlakte van die rechthoek is de oppervlakte van de rechthoekige driehoek.</p>
	<p>De woordformule bij het berekenen van de <b>oppervlakte van een rechthoekige driehoek</b> is:</p>
	<p>&nbsp; Oppervlakte rechthoekige driehoek = lengte &times; breedte &divide; 2</p><img src="afbeeldingen/meetkunde_oppervlakte/rechthoekige_th.png" width="250px" class="rightfloat">
	<p><b>Let op</b>: De lengte en de breedte moeten allebei een <b>rechthoekszijde</b> zijn van de driehoek.</p>
