<!-- theorie B -->
<div class="tekstblok theorie meetkunde">
	<h3>Theorie B Oppervlakte driehoeken</h3>
	<p><img src="afbeeldingen/meetkunde_oppervlakte/gewone_th.png" width="250px" class="rightfloat">Om elke driehoek kun je een rechthoek tekenen. Het is de kunst om dat zó te doen dat je de lengte én de breedte weet van die rechthoek, zodat je de oppervlakte uit kunt rekenen.</p>
	<p>Als je de oppervlakte weet van de rechthoek, kun je ook de oppervlakte van de driehoek berekenen. De oppervlakte van &Delta;<em>MQP</em> is precies even groot als &Delta;<em>MPS</em> en <nobr><em>oppervlakte</em> &Delta;<em>NPQ</em></nobr> = <nobr><em>oppervlakte</em> &Delta;<em>NRP</em></nobr>. Dus de oppervlakte van de driehoek is de helft van de oppervlakte van de rechthoek.</p>
	<p>In het plaatje hiernaast heet lijnstuk <em>MN</em> de <b>basis</b> en <em>PQ</em> de <b>hoogtelijn</b> van de driehoek. De lengte van <em>PQ</em> vertelt hoe hoog punt <em>P</em> boven de basis ligt en heet dus de <b>hoogte</b>.</p>
	<p>De woordformule bij het berekenen van de oppervlakte van een willekeurige driehoek is (dus ook van een rechthoekige driehoek):</p>
	<p>&nbsp; Oppervlakte driehoek = basis &times; hoogte &divide; 2<br>
		of<br>
		&nbsp; Oppervlakte driehoek = <math><mfrac><mn>1</mn><mn>2</mn></mfrac></math> &times; basis &times; hoogte</p>
	<p><b>Let op</b>: De basis en de hoogte moeten loodrecht op elkaar staan.</p>
</div>

