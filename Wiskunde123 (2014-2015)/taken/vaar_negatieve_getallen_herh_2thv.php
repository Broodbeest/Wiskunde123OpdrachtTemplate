	<table>
		<tr>
			<td width="390">
				Maar als je van een getal een negatief getal aftrekt, dan wordt het juist groter.
			</td>
			<td width="30">
				 
			</td>
			<td width="290">
				<u>Voorbeeld</u><br>
				6 - -8 = 14		en        14 > 6<br>
			</td>
		</tr>
	</table>
	<p>
	Het valt op dat  6 - -8 gelijk is aan 6 + 8. En ook dat 8 + -3 gelijk is aan 8 - 3.
	<br>
	Je kunt zeggen dat het aftrekken van een negatief getal hetzelfde is als het optellen van een positief getal. 
	</p>
	<p><u>Voorbeeld</u><br>
	7 - -2 = 7 + 2 = 9<br>
	-8 - -6 = -8 + 6 = -2
	</p>
	<p>
	En je kunt zeggen dat het optellen van een negatief getal hetzelfde is als het aftrekken van een positief getal. 
	<p>
	<p><u>Voorbeeld</u><br>
	12 + -4 = 12 - 4 = 8<br>
	-15 + -9 = -15 - 9 = - 24
	</p>
