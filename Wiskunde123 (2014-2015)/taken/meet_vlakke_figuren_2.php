	<h3>Theorie C Oppervlakte van een cirkel</h3>
	<p>
		Er is een formule voor de oppervlakte van een cirkel:<br>
		<em>Oppervlakte cirkel</em> = `&pi;` &sdot; <em>straal</em><sup>2</sup>
	</p>
	<p>
		Het teken `&pi;` is een getal, namelijk 3,141592... Je spreekt `&pi;` uit als <b>pi</b>. Op je rekenmachine is het getal `&pi;` opgeslagen onder een knop. Als je meer <b>decimalen</b> van `&pi;` wilt zien, neem dan een kijkje op <a href="http://www.cijfers.net/pi.html" target="_blank">Cijfers.net</a>.
	</p>
	<p>
		Het antwoord op de opdracht voor deze theorie is dus <nobr>`&pi;` &sdot; 4<sup>2</sup> = 50,26548... cm<sup>2</sup></nobr>.<br>
	</p>
	<p>
		<img src="afbeeldingen/meet_vlakke_figuren_2/opp_cirkel_vb.png" width="150px" class="rightfloat">
		<u>Voorbeeld</u>:<br>
		Bekijk de cirkel hiernaast en bereken de oppervlakte ervan.
	</p>
	<p>
		<u>Uitwerking</u>:<br>
		De <b>diameter</b> is 4 cm, dus de straal is 2 cm.<br>
		De oppervlakte van de cirkel is <nobr>`&pi;` &sdot; 2<sup>2</sup> = 12,57 cm<sup>2</sup></nobr>.
	</p>
</div>

