	<h3>Taakbollen</h3>
	<p>
		De <b>taakbollen</b> hebben een bepaalde kleur en grootte. <br>
Aan de kleur kun je zien bij welk domein de taak hoort. Een domein is een deelgebied van wiskunde. De blauwe taakbollen zijn taken bij het domein Vaardigheden, de groene taakbollen bij het domein Meetkunde en de oranje taakbollen horen bij het domein Analyse.<br>
Aan de grootte kun je zien hoeveel tijd er ongeveer voor de taak staat. Een kleine bol is ongeveer 1 uur werk, de iets grotere 2 uur, et cetera. De hoeveelheid werk is afgestemd op het aantal lesuren van een thema. Een thema heeft standaard 24 wiskundelessen van 45 minuten. De taakbelasting van het wiskundeprogramma is 24 klokuur. Je zult dus ongeveer een kwart van het werk aan wiskunde buiten de wiskundeles doen.
	</p>

	<h3>Volgorde</h3>
	<p>
Je werkt aan de taken binnen één domein volgens de aangegeven volgorde: taak V2.3 Termen van een som komt voor taak V2.4 Kolomsgewijs vermenigvuldigen. Je mag afwisselen tussen verschillende domeinen, bijvoorbeeld: je begint met de eerste vier taken uit het domein Vaardigheden en maakt dan een Meetkundetaak, dan weer wat Vaardigheden en als laatste pas de Analysetaak. Let op: je docent kan hiervan afwijken, bijvoorbeeld omdat er een bepaalde klassikale instructie wordt gegeven.</p>

	<h3>Werken aan wiskunde</h3>
	<p>
Wiskunde is een doe-vak. Dat betekent dat je vooral veel, heel veel moet oefenen. En oefening baart kunde, wiskunde in dit geval. Elke taak bestaat theorieblokken en opdrachtblokken. Het is de bedoeling dat je die theorie goed doorneemt. Let daarbij goed op de voorbeelden die gegeven worden. Met behulp van de theorie en het voorbeeld kun je vervolgens zelf de opdrachten uitvoeren. De theorie kan worden aangevuld met instructie. Dit geldt ook voor het maken en uitwerken van de opdrachten.<br>
De opdrachten maak je in je wiskundeschrift, je tekenschrift, losse werkbladen en/of op de computer, bijvoorbeeld via een spelletje, een applet of een ander programma. Denk eraan dat je soms eerst even op een kladblaadje moet oefenen voordat je een nette versie maakt, vooral bij meetkunde en bij sommige berekeningen is dit belangrijk.<br>
Structuur in je maakwerk is essentieel. Wiskunde is een manier van denken én van werken. Veel opdrachten die je maakt, neem je eerst over in je schrift. Met tussenstappen en berekeningen laat je zien wat je beheerst. Let hierbij op dat je niet teveel gaat schrijven, maar dat je een wiskundige opbouw en oplossing laat zien.<br> 
Je maakt berekeningen met pen. Tekeningen, schetsen en constructies maak je met potlood. Denk eraan dat je een scherpe punt gebruikt en dat een rechte lijn pas recht is als je deze met, bijvoorbeeld, een liniaal trekt. Cirkels maak je met een passer.
	</p>

<h3>Wat verwachten we van jou?</h3>
	<p>
Wiskunde is misschien wel het mooiste vak hier op school; het is een manier van denken en werken, die jij je ook eigen kunt en gaat maken. We verwachten van jou een actieve en nieuwsgierige houding. Ga vooral aan de slag. Blijf niet naar je computer staren, maar begin zo snel mogelijk. Je zult zien dat het meevalt. Raak vertrouwd met de manier van werken, bijvoorbeeld hoe de taken zijn opgebouwd. Vraag niet meteen om hulp, kijk eerst eens even goed naar wat er allemaal staat, naar de voorbeelden, probeer eens wat, overleg met een klasgenoot. Natuurlijk mag je daarna gewoon al je vragen stellen. Veel wiskunde is niet meer dan veredeld puzzelen. Soms herken je dingen uit het dagelijks leven, soms leer je iets wat je later nodig hebt en soms is er gewoon wiskunde voor de wiskunde: leuk!
	</p>
	
	<h3>Zelf kritisch nakijken</h3>
	<p>
	De opdrachten die je maakt, ga je zelf kritisch nakijken. In het begin is dat best lastig. Waar moet je nu precies op letten? Voor veel opdrachten zijn er uitwerkingen beschikbaar, klik op het pijltje bij de opdracht. Kritisch nakijken betekent dat niet alleen het antwoord maar ook de weg er naar toe in orde moet zijn. Denk bijvoorbeeld aan het verschil tussen kleine letters en hoofdletters.
	</p>

<h3>Werktempo</h3>
<p>
Er zijn vier wiskundelessen per week. Deze staan in de eerste klas meestal ingeroosterd als blokken van twee lesuren. Eén lesuur duurt 45 minuten. De meeste leerlingen zullen ook buiten de wiskundeles om aan wiskunde moeten werken om bij te blijven of beter te worden. Je kunt makkelijk elke dag een kwartier aan wiskunde werken. Het is beter om verspreid over de week te werken, dan op één dag achter elkaar. Sommigen hebben meer tijd nodig dan anderen. Je leert snel genoeg hoeveel tijd jij nodig hebt.
</p>

<h3>Geen werkboek</h3>
<p>
Jullie hebben alleen een ruitjesschrift, een tekenschrift en de wiskundesite. Soms zijn er opdrachten waar 'werkblad' bij staat. Deze werkbladen zul je zelf moeten afdrukken, tenzij je docent anders aangeeft. Houd er rekening mee dat je op school niet kunt printen.
</p>

<h3>Invullen betekent overnemen</h3>
<p>
Het is hierboven al een paar keer genoemd, maar nogmaals: als je sommetjes maakt, neem je deze altijd eerst over in je schrift en werkt dan, meestal via tussenstappen, naar het antwoord toe.
</p>

<h3>Wiskundespullen</h3>
<p>
Voor wiskunde heb je het volgende nodig:
<ul>
	<li>een schrift met grote ruiten én een kantlijn (die kantlijn kun je eventueel zelf tekenen);</li>
	<li>een schrift met blanco tekenpapier;</li>
	<li>een pen om mee te schrijven en een pen om mee na te kijken, gebruik verschillende kleuren;</li>
	<li>grijze potloden om mee te schetsen, tekenen en te construeren, gebruik bij voorkeur potloden met verschillende hardheid: hb of b1 om te schetsen en hb of h1 voor strakkere lijnen;</li>
	<li>kleurpotloden onder andere om constructies mee in te kleuren, te verduidelijken, om hulplijnen aan te geven en het tekenen van grafieken;</li>
	<li>een goede passer voor het construeren;</li>
	<li>een lat om rechte lijnen mee te trekken;</li>
	<li>later een geodriehoek en liniaal;</li>
	<li>gum, puntenslijper.</li>
</ul>
Als je schrift vol is, wordt er van je verwacht dat je zelf een nieuwe regelt. Gooi je oude schriften nooit weg, maar bewaar ze voor latere referentie en als bewijs dat je bepaalde opdrachten hebt gemaakt. Daarbij worden je schriften ook gebruik bij het bespreken van je werk. Zet je naam mét achternaam en het nummer van je klas op je schrift en al het werk dat je maakt en/of inlevert. Zorg goed voor je eigen spullen en houd er rekening mee dat je tijdens toetsen niet van elkaar mag lenen.
</p>