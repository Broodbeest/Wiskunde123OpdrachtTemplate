		<p>De Umayyaden Moskee in Damascus, ook wel de Grote moskee van Damascus genoemd, is de belangrijkste moskee van de Syrische hoofdstad Damascus. De moskee is een belangrijk gebouw, zowel wat betreft de architectuur als de religieuze betekenis van dit heiligdom. Na de Arabische verovering van Damascus in 634 werd de moskee tussen 706 en 715 gebouwd door de Umayyadenkalief al-Walid I op de plaats van een christelijke basiliek toegewijd aan Johannes de Doper. <br>Bijzonder is dat het hoofd van Johannes de Doper er als relikwie bewaard zou zijn. Johannes de Doper wordt zowel door de christenen als door de moslims als profeet beschouwd.</p>
