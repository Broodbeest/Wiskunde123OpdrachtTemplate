<!-- theorie C -->
	<div class="tekstblok theorie analyse">
		<h3>Theorie C</h3>
		<p>Bij wiskunde tekenen we heel vaak assenstelsels met stapgrootte één. De horizontale as noemen we ook wel
		<b>x-as</b> en de verticale as noemen we ook vaak de <b>y-as</b> (spreek uit: “ei”-as).<br>
		Een <b>roosterpunt</b> is een punt op de kruising van twee lijnen van je roosterpapier. Bij een roosterpunt horen
		altijd hele getallen.<br>
		<br>
		Hiernaast zie je een assenstelsel met twee roosterpunten. Zoals je hebt geleerd noteren we punten met een hoofdletter.
		Hier zie je de punten A en B.
		<img src="afbeeldingen/analyse_assenstelsel_en_grafiek_1thv/theorie_C_plaatje_1.png" width="200" /><br>
		Om te kunnen vertellen waar de punten A en B liggen gebruiken we coördinaten. We lezen op de x-as en de y-as af waar het punt ligt.
		Je begint altijd vanuit de oorsprong en gaat eerst naar rechts en dan omhoog.
		<img src="afbeeldingen/analyse_assenstelsel_en_grafiek_1thv/theorie_C_plaatje_2.png" width="200" /><br>
		Punt A heeft <b>x-coördinaat</b> 1 en <b>y-coördinaat</b> 2. Je schrijft dan `A(1 , 2)`.<br>
		<br>
		De getallen binnen de haakjes noemen we de coördinaten. Het eerste getal is de x-coördinaat en het tweede getal is
		de y-coördinaat.<br>
		<br>
		Je kan de volgorde makkelijk onthouden door te bedenken dat de x van x-coördinaat eerder in het alfabet staat dan de
		y van y-coördinaat. Of je bedenkt dat de h van horizontaal eerder in het alfabet staat dan de v van verticaal!<br>
		<br>
		Punt B heeft coördinaten:  `B(4 , 3)`.<br>
		<br>
		Als een punt geen roosterpunt is, zijn de coördinaten geen hele getallen. Het punt `K` hiernaast is zo’n punt. <br>
		<img src="afbeeldingen/analyse_assenstelsel_en_grafiek_1thv/theorie_C_plaatje_3.png" width="200" /><br>
		De x-coördinaat van punt `K` is `2,5` en de y-coördinaat van punt `K` is `4`. De eerste coördinaat is dus geen heel getal. 
		Als je van punt `K` de coördinaten wil opschrijven gebruik je tussen de coördinaten geen komma, maar een <b>punt-komma</b> ( ; ). 
		Dit doe je, omdat je al een komma nodig hebt voor de decimale getallen. Je krijgt dan `K( 2,5;4 )`.
		</p>
	</div>


