<!-- theorie A -->
<div class="tekstblok theorie analyse">
	<h3>Theorie A</h3>
	<p>
		In een grafiek worden gegevens weergegeven die je kunt aflezen. Het is belangrijk dat je weet wat de getallen
		betekenen, daarom zetten we dat er altijd bij. Bij ieder getal hoort een <b>grootheid</b> en een <b>eenheid</b>. 
		Dit staat er ook altijd bij in een grafiek.
		<br>
		<br>
		De <b>grootheid</b> is <b>wat</b> je meet. In opdracht 1 waren dat de <em>afstand</em> en de <em>hoogte</em>.
		De <b>eenheid</b> is <b>waarin</b> je dat meet. De afstand werd gemeten in <em>km</em> en de <em>hoogte</em> in meters.
		<br>
		<br>
		Dus, als je wil weten hoe zwaar je bent, dan meet je de grootheid gewicht. Dat doe je in de eenheid kilogram.
		En als je vertelt hoe oud je bent, dan heb je het over de grootheid leeftijd in de eenheid jaren.
	</p>
</div>

