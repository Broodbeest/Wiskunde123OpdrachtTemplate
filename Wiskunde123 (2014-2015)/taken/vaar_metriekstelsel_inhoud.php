	<h3>Theorie C Oppervlaktematen</h3>
	<p>
		De eenheden bij oppervlakte zijn bijvoorbeeld cm<sup>2</sup> of m<sup>2</sup>. Dat spreek je uit als <b>vierkante</b> centimeter en <b>vierkante</b> meter.
	</p>
	<p>
		<img src="afbeeldingen/vaardigheden_metriekstelsel/opp_omrekenen.png" width="250 px" class="rightfloat">
		In één centimeter passen 10 millimeters, dus <nobr>1 cm<sup>2</sup></nobr> = <nobr>10 mm &sdot; 10 mm</nobr> = <nobr>100 mm<sup>2</sup></nobr> (zie de afbeelding hiernaast).
	</p>
	<p>
		<u>Let op</u>: Bij oppervlaktes is één stap hoger of lager op de trap &times; of &divide; <font color="blue"><b>100</b></font>.
	</p>
	<p>
		<u>Ezelsbruggetje</u> daarbij is dat oppervlakte altijd uitgedrukt wordt in m<sup>2</sup>, een m van meter met daarboven een <font color="blue"><b>2</b></font> van <font color="blue"><b>twee</b></font> nullen.
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
		<div class="tekstblok opdracht">
			<ol type="a">Reken uit wat er op de puntjes moet staan.
				<li>2,5 m<sup>2</sup> = ... cm<sup>2</sup></li>
			</ol>
		</div>
	</p>
	<p>
		<u>Uitwerking</u><br>
		Van m<sup>2</sup> naar cm<sup>2</sup> is twee traptreden. Dus moet 2,5 twee keer met 100 worden vermenigvuldigd: <nobr>2,5&times;100&times;100 = 25000 cm<sup>2</sup></nobr>.
	</p>
