<div class="tekstblok theorie vaardigheden">
	<h3>Theorie G Tegengestelde bewerkingen</h3>
	<p>
		Een <b>bewerking</b> is iets dat je met een getal doet, bijvoorbeeld optellen, aftrekken, vermenigvuldigen of delen.
	</p>
	<p>
		Aftrekken is het <b>tegengestelde</b> van optellen. Anders gezegd: de som en het verschil zijn <b>tegengestelde bewerkingen</b>.
	</p>
	<p>
		Als `8 - 5 = 3`, dan `3 + 5 = 8`.
	</p>
</div>

