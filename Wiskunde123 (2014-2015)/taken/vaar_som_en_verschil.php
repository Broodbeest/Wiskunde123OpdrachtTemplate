<div class="tekstblok theorie vaardigheden">
	<h3>Theorie B De termen van een som</h3>
	<img src="afbeeldingen/termen_van_een_som/optelling.JPG" alt="verticale_getallenlijn" class="rightfloat" width="200px" height="" />
	<p>
		De <b>som</b> van twee of meer getallen is de <b>optelling</b> van die getallen.
	<br>
		De som van `3` en `5` is `8`, want `3 + 5 = 8`.<br>
		De getallen `3` en `5` noem je de <b>termen</b> van de som.
	</p>
	
	<p>
		<u>Voorbeeld:</u>
		<br>
		De som van `4` en `11` is `15`, want `4 + 11 = 15`.
	</p>
</div>

