	<h3>Theorie C Splitsen in termen</h3>
	<p>
		Omgekeerd kun je ook bedenken uit welke <b>termen</b> een bepaald getal zou kunnen bestaan. Dat noem je <b>splitsen</b>. We splitsen alleen in mooie getallen als 2 of 7. Die mooie getallen noemen we in de wiskunde <b>natuurlijke</b> getallen.  
	</p>
