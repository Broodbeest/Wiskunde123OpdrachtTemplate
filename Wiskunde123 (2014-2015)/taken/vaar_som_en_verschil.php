	<h3>Theorie D Het verschil</h3>
	<p>
		Het verschil tussen twee getallen bereken je door het kleinste getal van het grootste getal af te trekken.<br> 
		Het verschil tussen `5` en `8` is `3`, want `8 - 5 = 3`. 
	</p>
	<p>
		<u>Voorbeeld</u>: <br>
		De verschil tussen `4` en `11` is `7`, want `11 - 4 = 7`.
	</p>
