<div class="tekstblok theorie statistiek">
	<h3>Theorie Gemiddelden en gewogen gemiddelden</h3>
	<p>
		Het gemiddelde van een aantal getallen bereken je door de getallen bij elkaar op te tellen en dat te delen door het aantal getallen.
	</p>
	<p>
		Je bepaalt dus eerst de som van alle getallen en deelt het vervolgens door het aantal getallen.
	</p>
	<p>
		Soms tellen niet alle getallen even zwaar mee. Dan spreek je van een gewogen gemiddelde. In een gewogen gemiddelde tel je een getal met groter gewicht vaker mee.
	</p>
	<p>
		Voorbeeld:<br>
		Voor een toets haal je een 7 en voor een overhoring een 6. Nu telt de toets drie keer mee en een overhoring maar één keer. Het gewogen gemiddelde van deze twee cijfers bereken je als volgt:
	</p>
	<p>
		`(3*7+1*5)/4=26/4=6,5`
	</p>
</div>


