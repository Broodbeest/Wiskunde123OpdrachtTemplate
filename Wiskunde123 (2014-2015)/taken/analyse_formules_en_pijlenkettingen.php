	<h1>Formules en pijlenkettingen</h1>

	<div class="tekstblok theorie analyse">
		<h3>Woordformules maken</h3>
		<p>Bij de analyse taak <a href="http://wiskunde.vathorstcollege.nl/index2.php?p=analyse_woordformules" target="_blank">Woordformules</a> zijn we bezig geweest met het zoeken van regelmaat en het maken van woordformules. 
			Een woordformule is eigenlijk een recept om iets uit te rekenen. Zo hebben we bijvoorbeeld bij het volgende plaatje een woordformule 
			bedacht: <br>
			<img src="afbeeldingen/analyse_2/theorie_A_tafelschikkingen.png" width="600" /></p>
		<p>Bij bovenstaand plaatje ontdekten we de volgende regelmaat:De <b><FONT color="red">éne</FONT></b> stoel aan kop van de tafel zit in ieder figuur.</p>
		<p>Als er een tafel bijkomt komt er een stoel onder en boven bij. De regelmaat is dus steeds <b><FONT color="green">2</FONT></b> erbij.</p>  
		<p>De woordformule is dus:	<em>Aantal stoelen</em> = <b><FONT color="red">1</FONT></b> + <b><FONT color="green">2</FONT></b> &times; <em>nummer figuur</em>. </p>
		<p>Bij ieder recept hoort een werkvolgorde. Denk maar aan het bakken van een taart; eerst moet je het deeg maken, dan de vulling maken en in je taart stoppen en dan afbakken. Hanteer je de verkeerde volgorde, bijvoorbeeld eerst het deeg maken, dan afbakken en daarna de vulling erin dan geeft dit geen goed resultaat.  <br>
			Dit geldt ook voor het gebruiken van een formule. Dus ga je rekenen met formules denk dan goed aan de rekenvolgorde!</p>
	</div>

