		<h3>Theorie E Ingewikkelde rekenpijlen (havo/vwo)</h3>
		<p>Zoals je gezien hebt in bovenstaande sommen is het erg belangrijk dat je weet wat de rekenvolgorde is!
		Deze rekenvolgorde vind je terug in de pijlenketting. Dus eerst altijd haakjes wegwerken, dan vermenigvuldigen 
		en delen (van links naar rechts werken) en tot slot optellen en aftrekken (ook van links naar rechts werken)
