	<h3>Inleiding</h3>
	<p>
		Deze taak gaat over de tafels van vermenigvuldiging en een aantal extra kwadraten. Het is belangrijk en handig de tafels en kwadraten uit je hoofd te kennen. Veel van deze tafels ken je hopelijk nog van de basisschool. Terwijl je die kennis weer opfrist, leer je ook hoe je netjes werkt aan wiskunde en een aantal begrippen. Hieronder staan de <nobr>(leer-)doelen</nobr> van deze taak.
	</p>
	<h3>Doelen</h3>
	<ul>
		<li>
			het (netjes) leren werken aan wiskunde;
		</li>
		<li>
			kennen van de tafels van 1 t/m 10, 12, 15 en 20;
		</li>
		<li>
			begrippen: product, veelvoud, vermenigvuldigen en kwadraat;
		</li>
		<li>
			kennen van de kwadraten van 1 t/m 20.
		</li>
	</ul>

	<h3>Werkwijze</h3>
	<p>
		Sommige opdrachten verwijzen naar applets en/of spelletjes op het internet. Voor deze opdrachten hoef je, tenzij anders vermeld, niks in je schrift te schrijven.
	</p>
	<p>
		De taak wordt afgesloten met een toets.
	</p>
	<p>
		Het is aan te raden om eerst de taak helemaal te maken en daarna, bijvoorbeeld, elke dag vijf minuten te leren, eventueel met behulp van applets en/of spelletjes. Helemaal onderaan kun je zelf een oefentoets maken.
