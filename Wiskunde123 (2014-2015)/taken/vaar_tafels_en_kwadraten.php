<!-- theorie C kwadraten -->
<div class="tekstblok theorie vaardigheden">
	<h3>Theorie C Kwadraten</h3>
	<p>
		Producten als '5 maal 5' of '8 maal 8' zijn bijzonder. Omdat je dan een getal met zichzelf vermenigvuldigt. Deze bijzondere vermenigvuldiging noem je een <b>kwadraat</b>.
	</p>
	<p>
		Een kwadraat is het product van een getal met hetzelfde getal.
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
		Het kwadraat van 3 is 9, want het product van 3 en 3 is 9. <br>
		Het kwadraat van 6 is 36, want het product van 6 en 6 is 36.
	</p>
</div>

