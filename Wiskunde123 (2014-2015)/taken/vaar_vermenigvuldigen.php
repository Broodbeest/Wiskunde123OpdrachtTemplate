<h1>Vermenigvuldigen</h1>

<div class="tekstblok theorie vaardigheden">
	<h3>Inleiding</h3>
	<p>
		Producten als 3 maal 6 en 7 maal 10 ken je als het goed is uit je hoofd. Het zijn zogenaamde <b>tafelproducten</b>, je hoeft ze niet steeds opnieuw te bereken. Andere producten weet je niet zomaar uit je hoofd. Vooral als het grote of lastige getallen zijn.
	</p>
	<h3>Doelen:</h3>
	<ul>
		<li>
			verder inoefenen van de tafels van vermenigvuldiging;
		</li>
		<li>
			leren vermenigvuldigen volgens één altijd werkende aanpak.
		</li>
	</ul>
	
	<h3>Werkwijze</h3>
	<p>
		Lees de theorie, bekijk de clips en maak de opdrachten in je schrift. Kijk je werk steeds goed na. Deze taak zal je in ongeveer 45 minuten kunnen maken.
	</p>
	
</div>

<div class="tekstblok opdracht">
	<h3>Opdracht 1</h3>
	<p>
		Bereken 17 &times; 31 en ook 1023 &times; 197 op de manier die jij op de basisschool hebt geleerd.<br>
	Schrijf duidelijk op hoe je het berekent. Vergelijk het met een klasgenoot die van een andere basisschool komt. Welke methode is makkelijker? En welke sneller? Leg je antwoord uit.
	</p>
	
		<div class="hider">
		<div class="showhide">&rarr;</div>
		<div class="antwoord">
			<h3>Antwoorden bij opdracht 1</h3>
			<p>
				17 &times; 31 = 527<br>
				1023 &times; 197 = 201.531<br>
				De punt tussen de `1` en de `5` geeft het onderscheid tussen honderd- en duizendtallen aan. Zo wordt het getal makkelijker leesbaar.
			</p>
		</div>
	</div>
	
</div>

