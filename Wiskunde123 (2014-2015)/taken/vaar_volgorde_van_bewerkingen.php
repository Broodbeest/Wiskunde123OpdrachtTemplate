	<h3>Theorie E Meerdere bewerkingen</h3>
	<p>
	Je hebt nu een aantal opgaven gemaakt met twee bewerkingen. Je kunt in één opgave ook meerdere bewerkingen maken. Om dan goed overzicht te houden, maak je gebruik van <b>tussenstappen</b> en onderstreep je steeds de <b>bewerking</b> met <b>voorrang</b>.
	</p>
	<p>Hieronder wordt stap voor stap voorgedaan hoe je een opgave als<br> `6 × 3 + 4 : 2 - 8 =` moet uitrekenen.
	</p>
	<table style="vertical-align=top" class="stappenplan">
		<tr>
			<td>Stap 1</td>
			<td>Neem de opgave over in je schrift.</td>
			<td width="200">6 × 3 + 4 : 2 - 8 =</td>
		</tr>
		<tr>
			<td>Stap 2</td>
			<td>Onderstreep de bewerking met voorrang.</td>
			<td><u>6 × 3</u> + 4 : 2 - 8 =</td>
		</tr>
		<tr>
			<td>Stap 3</td>
			<td>Bereken het onderstreepte gedeelte, neem de rest over en onderstreep de volgende bewerking die voorrang heeft.</td>
			<td>18   + <u>4 : 2</u> - 8 =</td>
		</tr>
		<tr>
			<td>Stap 4</td>
			<td>Bereken het onderstreepte gedeelte, neem de rest over en onderstreep de volgende bewerking die voorrang heeft.</td>
			<td><u>18    +    2</u>  - 8 =</td>
		</tr>
		<tr>
			<td>Stap 5</td>
			<td>Bereken het onderstreepte gedeelte, neem de rest over en zet het antwoord achter het laatste ‘=’-teken.</td>
			<td>20  - 8 = 12</td>
		</tr>
	</table>
	<p>
	Merk op dat de tussenstappen onder elkaar staan en dat het eindantwoord alleen achter het laatste '='-teken staat.
	</p>
	<p>
	Nog een voorbeeld, bereken met tussenstappen:<br>
	18 - 9 : 3 + 2 × 7 =
	</p>
	<table class="stappenplan">
	<tr>
