<div class="tekstblok theorie vaardigheden">
	<h3>Theorie F Haakjes</h3>
	<p>
		In het verkeer heeft rechts voorrang. Wel zijn er <b>uitzonderingen</b>, bijvoorbeeld bij voorrangswegen of stoplichten, politiewagens met sirene of voetgangers op een zebrapad. Bij het rekenen kun je ook aangeven dat bepaalde bewerkingen toch <b>voorrang</b> hebben boven andere. Dat doe je met <b>haakjes</b>. Bekijk de voorbeelden hieronder.
	</p>
	<p>
		6 + 2 × 3 = <br>
		6 +    6    = 12
	</p>
	<p>
		maar<br>
		(6 + 2) × 3 =<br> 
		8 × 3 = 24
	</p>
	<p>		
		De <b>haakjes</b> om het stukje '6 + 2' geven aan dat je <b>eerst</b> de som van 6 en 2 moet nemen, <b>voordat</b> je gaat vermenigvuldigen met 3.
	</p>
	<p>
		<b>De voorrangsregel op een rij:</b><br>
		1.	Tussen de haakjes;<br>
		2.	Vermenigvuldigen of delen van links naar rechts;<br>
		3.	Optellen of aftrekken van links naar rechts.<br>
		Schrijf alle tussenstappen onder elkaar op, onderstreep steeds de bewerking die voorrang heeft en zet het antwoord na het laatste '='-teken.
	</p>
	<p>
	<u>Voorbeeld 1</u><br>
	Bereken met behulp van tussenstappen (5 - 3) × 3 = 
	</p>
	<table class="stappenplan">
		<tr>
			<td>Stap 1</td>
			<td>(<u>5 - 3</u>) × 3 =</td>
		</tr>
		<tr>
			<td>Stap 2</td>
			<td> 2    × 3 = 6</td>
		</tr>
	</table>
	<p>		
	<u>Voorbeeld 2</u><br>
	Bereken met behulp van tussenstappen 6 × (3 + 4) : 2 - 8 =
	</p>
	<table class="stappenplan">
		<tr>
			<td>Stap 1</td>
			<td>6 × (<u>3 + 4</u>) : 2 - 8 =</td>
		</tr>
		<tr>
			<td>Stap 2</td>
			<td> <u>6   ×   7</u>      : 2 - 8 =</td><td>(let op: haakjes vallen weg)</td>
		</tr>
		<tr>
			<td>Stap 3</td>
			<td><u>42         : 2</u> - 8 =</td>
		</tr>
