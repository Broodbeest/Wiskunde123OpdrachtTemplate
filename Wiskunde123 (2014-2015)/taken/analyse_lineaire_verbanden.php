<!-- Theorie afname is negatieve toename-->
<div class="tekstblok theorie analyse">
	<img src="../afbeeldingen/analyse_4/achteruitlopen.JPG" alt="achteruitlopen" width="200" height="" class="rightfloat"/>
	<h3>Theorie C Afname is negatieve toename</h3>
	<p>
		De man in de afbeelding loopt achteruit. Wiskundigen zouden dit omschrijven als negatief vooruitlopen. Vergelijk dit met bijvoorbeeld een schuld, die kun je beschouwen als negatief bezit.
	</p>
	<p>
		We spreken af dat je een afname van `2` schrijft als een toename van `-2`. 
		Met andere woorden: een <nobr><b>afname</b> is een <b>negatieve toename</b>.</nobr> Een afname van 4 is hetzelfde als een toename van -4.
	</p>
	<p>
		Bij lineaire formules geeft het hellingsgetal de regelmaat weer. In het geval van een toename is dat hellingsgetal positief en in het geval van een afname is het hellingsgetal negatief.
	</p>
</div>

