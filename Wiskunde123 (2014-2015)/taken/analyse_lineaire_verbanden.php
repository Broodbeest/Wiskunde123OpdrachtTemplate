<!-- Theorie D invoer en uitvoer-->
<div class="tekstblok theorie analyse">
	<h3>Theorie D Invoer en uitvoer</h3>
	<p>
		 Bij een formule is een tabel gemaakt: <br> 
		<table FRAME="void" RULES="all"  width="400px" style="text-align: center">
			<TR>
				<TD ><em>invoer</em></TD><TD>1</TD><TD>2</TD><TD>3</TD><TD>4</TD><TD>5</TD>
			</TR>
			<TR>
				<TD><em>uitvoer</em></TD><TD>43</TD><TD>46</TD><TD>49</TD><TD>52</TD><TD>55</TD>
			</TR>
		</table>
		De bovenste rij zijn de getallen waarmee de formule heeft gerekend. Dat zijn de getallen die er <b>in</b> worden gestopt, de <b>invoer</b>. In de onderste rij staan de uitkomsten, de <b>uitvoer</b>.
	</p>
	<p><img src="../afbeeldingen/analyse_4/invoer-uitvoer.png" alt="invoer-uitvoer" width="400" height="" /></p>
	<p>
		Een formule kun je ook zien als een fabriek waar je grondstoffen invoert en producten uitkomen.
		
	</p>
	<p>
		Als de invoer met <b>één</b> toeneemt, dan neemt de uitvoer met 3 toe. Die regelmatige toename bij de uitvoer is het <b>hellingsgetal</b>. In dit geval is dat 3. In een grafiek is het hellingsgetal het vaste getal dat de grafiek omhoog of omlaag gaat, als je één stapje naar rechts gaat.
	</p>
	<p>
		 Als je uitrekent welke uitvoer er hoort bij invoer nul, dan heb je het <b>startgetal</b> berekend. In een grafiek is het startgetal de waarde waar de lineaire grafiek op de verticale as begint. Weet je dit niet meer? Kijk dan eens terug bij <a href="index2.php?p=analyse2" target="_blank">A2 woordformules, theorie B!</a>
	</p>
	<p>
		Als je het hellingsgetal en het startgetal weet, dan kun je de formule maken:
		<nobr><em>uitvoer</em> = <b>hellingsgetal</b> x <em>invoer</em> + <b>startgetal</b></nobr>
	</p>
</div>

