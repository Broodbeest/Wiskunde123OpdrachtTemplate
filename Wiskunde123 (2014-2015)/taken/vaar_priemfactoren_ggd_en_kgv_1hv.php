	<h3>Theorie J Kgv bepalen met priemfactorontbinding</h3>
	<p>
		Bovenstaande methode is veel werk bij grote getallen. Handiger is dan om de getallen te schrijven als een ontbinding van priemfactoren.  
	</p>
	<p>
		<u>Voorbeeld</u>:<br>
		Bepaal de kgv van 6 en 10.
	</p>
	<table class="stappenplan">
		
		<tr>
		<td width="60">
			<u>Stap 1</u>:
		</td>
		<td width="250">
				Ontbind beide getallen in priemfactoren.
		</td>
		<td width="120">
				6 = 2 × 3<br>
				10 = 2 × 5
		</td>
		</tr>
		<tr>
		<td>
			<u>Stap 2</u>:
		</td>
		<td>
				Neem de factoren van het grootste getal.
		</td>
		<td>
				2 × 5
		</td>
		</tr>
		<tr>
		<td>
			<u>Stap 3</u>:
		</td>
		<td>
				Neem de missende factoren van het andere getal.
		</td>
		<td>
				3
		</td>
		</tr>
		<tr>
		<td>
			<u>Stap 4</u>:
		</td>
		<td>
				Vermenigvuldig de gevonden factoren met elkaar.
		</td>
		<td>
				2 × 5 × 3 = 30<br>
				Dus	kgv(6, 10) = 30
		</td>
		</tr>
	</table>	
	<p>
