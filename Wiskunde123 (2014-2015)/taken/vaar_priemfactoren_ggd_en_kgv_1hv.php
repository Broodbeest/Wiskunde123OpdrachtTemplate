<div class="tekstblok theorie vaardigheden">
	<h3>Theorie A Priemgetallen</h3>
	<p>
		Een getal als 5 kun je alleen maar ontbinden in 1 en zichzelf. Net als bijvoorbeeld 11 of 17. Zulke getallen noem je <b>priemgetallen</b>.
	</p><img src="afbeeldingen/ontbinden_in_priemfactoren/theorie_b.png" alt="theorie_b" width="" height="" class="rightfloat" />
	<p>				
		Een priemgetal is een getal met precies twee delers: het getal zelf en 1.
	</p>
	<p>
	De rij priemgetallen begint dus zo: 2, 3, 5, 7, 11, 13, 17, … Andere voorbeelden van priemgetallen zijn 173 en 2011. Het getal 1 is niet priem: dat heeft maar één deler, namelijk zichzelf.
	</p>
	<p>
		Priemgetallen zijn magische getallen, die door de eeuwen heen al bewust en onbewust zijn gebruikt en nu met groot succes worden ingezet om internetverkeer, banktransacties en ook e-mails te coderen. 
	</p>
	<p>
		In de ideale codeersituatie is maar één keer dataverkeer nodig en hoeft daarvoor geen sleutel uitgewisseld te worden. Dit is precies waar priemgetallen voor kunnen zorgen.<br>
		Wil je hier meer over weten ga dan naar: <a href="http://www.kennislink.nl/publicaties/de-simpele-magie-van-priemgetallen-in-geheimschrift" target="_blank">De simpele magie van priemgetallen in geheimschrift</a>.
